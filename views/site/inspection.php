        <p><strong>&nbsp;Правила и периодичность проведения профилактических медицинских осмотров целевых групп населения&nbsp;</strong></p>
        <p><strong>&nbsp;Раздел 1. Общие положения</strong></p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1. Настоящие Правила и периодичность проведения профилактических медицинских осмотров целевых групп населения (далее – Правила) определяют порядок и периодичность проведения профилактических медицинских осмотров целевых групп населения.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2. Термины и определения, используемые в настоящем приказе:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) Терминологическая система Бетесда – стандартизации результата цитологического исследования;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) динамическое наблюдение – систематическое наблюдение за состоянием здоровья населения, а также оказание необходимой медицинской помощи по результатам данного наблюдения;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;3) Автоматизированная информационная система "Поликлиника" - информационная система, предназначенная для автоматизированного ввода и обработки "Статистической карты амбулаторного пациента", хранения и получения отчетной документации медицинских организаций, проведения профилактических медицинских (скрининговых) осмотров детского и взрослого населения;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;4) шкала оценки суммарного сердечно-сосудистого риска– шкала оценки риска смертельного сердечно-сосудистого заболевания ближайших 10 лет (далее – шкала&nbsp;SCORE);&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;5) индекс Кетле – показатель оценки гармоничности физического развития организма, соотношение массы тела к росту;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;6) классификация системы анализа и протоколирования результатов лучевых исследований молочной железы - стандартизация описаний маммографических исследований(далее - классификация&nbsp;BI-RADS);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;7) профилактические медицинские осмотры целевых групп населения – это массовые профилактические медицинские осмотры, проводимые сплошным методом, направленные на выявление заболеваний на ранних стадиях и предупреждение развития заболеваний, факторов риска, способствующих возникновению заболеваний, формирование и укрепление здоровья населения (далее – скрининговые осмотры).</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;3. Скрининговые осмотры проводятся в рамках гарантированного объема бесплатной медицинской помощи субъектами здравоохранения, имеющими лицензию на данный вид деятельности в соответствии со статьей 28 Закона Республики Казахстан от 16 мая 2014 года "О разрешениях и уведомлениях".</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;4. Местные органы государственного управления здравоохранением областей, города республиканского значения и столицы (далее – Управление) обеспечивают:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) ежегодное проведение скрининговых осмотров целевых групп населения согласно настоящим Правилам;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) заполнение электронной базы данных скрининговых осмотров;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;3) создание постоянно действующей рабочей группы по координации, мониторингу и анализу скрининговых осмотров.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;5. Скрининговые осмотры с последующим динамическим наблюдением и оздоровлением осуществляют организации здравоохранения, оказывающие первичную медико-санитарную помощь (далее – организации ПМСП):</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) для городского населения – организации ПМСП независимо от форм собственности;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) для сельского населения – медицинские пункты, фельдшерско-акушерские пункты, врачебные амбулатории, районные поликлиники, с использованием передвижных медицинских комплексов на базе специального автотранспорта и железнодорожного транспорта (поезд).</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;6. Организации ПМСП:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) формируют целевые группы лиц, подлежащих скрининговым осмотрам, из числа прикрепленного к медицинской организации населения;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) обеспечивают преемственность с профильными медицинскими организациями для проведения данных осмотров;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;3) информируют население о необходимости прохождения скрининговых осмотров;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;4) заполняют учетные формы с внесением результатов осмотров в медицинскую карту амбулаторного пациента или историю развития ребенка;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;5) заполняют электронную базу скрининговых осмотров через медицинские информационные системы организаций здравоохранения, уполномоченного органа в области здравоохранения;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;6) проводят ежемесячный анализ проведенных скрининговых осмотров.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;7. Скрининговым осмотрам не подлежат лица, находящиеся на диспансерном учете, по профилю заболевания которого проводится данный скрининговый осмотр.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;8. Дополнительные диагностические исследования и осмотры профильных специалистов проводятся по показаниям.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;9. Средний медицинский персонал организации ПМСП или ответственное лицо организации ПМСП по результатам проведенного скринингового осмотра сверяет список пациентов целевой группы, приглашенных на скрининг - обследование, прошедших скрининг и направленных на дообследование.</p>
        <p><strong>&nbsp;Раздел 2. Порядок проведения скрининговых осмотров целевых групп населения</strong></p>
        <p><strong>&nbsp;Глава 1. Порядок проведения скрининговых осмотров детского населения</strong></p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;10. Скрининговые осмотры детей дошкольного возраста, посещающих дошкольные учреждения, школьников, учащихся и студентов средне-специальных и высших учебных заведений до 18 лет проводятся специалистами территориальной организации ПМСП с выездом на территории организаций образования.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;11. Скрининговые осмотры детей дошкольного возраста, не посещающих дошкольные учреждения проводятся в организации ПМСП по месту прикрепления.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;12. Проведение скрининговых осмотров детей до 18 лет включает в себя подготовительный этап, скрининг и заключительный этап.</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;13. Подготовительный этап состоит из формирования целевых групп детей до 18 лет и информационного сопровождения. Подготовительный этап осуществляется средним медицинским персоналом организации ПМСП: медицинского пункта, фельдшерско-акушерского пункта, врачебной амбулатории, районной, городской поликлиники, средним медицинским персоналом организации образования (при его наличии), в том числе дошкольного учреждения и включает в себя:&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;ежегодное формирование и составление списка целевых групп, подлежащих скрининговым осмотрам в предстоящем году, до 15 ноября календарного года с последующей ежемесячной коррекцией целевых групп;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;оповещение родителей детей целевых групп и их законных представителей о необходимости и условиях прохождения скрининговых осмотров;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;организацию медицинским работником организации образования (при его наличии), в том числе дошкольного учреждения и организации ПМСП выезда специалистов территориальной организации ПМСП на территории организаций образования (при его наличии), в том числе дошкольных учреждений для проведения скринингового осмотра детей дошкольного возраста (посещающие детские дошкольные учреждения), школьников, учащихся и студентов средне-специальных и высших учебных заведений в возрасте до 18 лет;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;14. Этап скрининга предусматривает осмотр специалистами и заполнение по итогам осмотра статистических карт профилактического медицинского осмотра (скрининга) ребенка по форме № 025-07/у, утвержденной приказом исполняющего обязанности Министра здравоохранения Республики Казахстан от 23 ноября 2010 года № 907 "Об утверждении форм первичной медицинской документации организаций здравоохранения" (зарегистрирован в Реестре государственной регистрации нормативных правовых актов за № 6697) (далее – Приказ № 907).</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;15. Этап скрининга включает в себя доврачебный, педиатрический и специализированный этапы.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;Доврачебный этап проводится средним медицинским персоналом медицинского пункта, фельдшерско-акушерского пункта, врачебной амбулатории, районной, городской поликлиники, средним медицинским персоналоморганизации образования (при его наличии), в том числе дошкольного учреждения и включает в себя:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) определение роста и массы тела;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) измерение объема головы и грудной клетки у детей раннего возраста (до 3 лет);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;3) оценку физического и психосоциального развития;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;4) измерение артериального давления (у детей 7 лет и старше);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;5) проведение плантографии и ее оценку (у детей 5 лет и старше);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;6) определение остроты зрения;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;7) исследование остроты слуха. У детей 3 лет и старше исследование проводится с помощью шепотной речи на оба уха поочередно, в тихом помещении на расстояниине менее 5 метров между средним медицинским работником и ребенком;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;8) оформление первичной медицинской документации по данным осмотра.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;Педиатрический этап проводится врачом педиатром или врачом общей практики и включает в себя: оценку состояния здоровья ребенка, психофизического, полового развития, выявление признаков жестокого обращения с учетом данных доврачебного обследования. В ходе этапа проводятся:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) осмотр кожных покровов и волосистой части головы;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) осмотр видимых слизистых оболочек: конъюнктивы глаз, полости рта, зева. Обращается внимание на состояние зубов, прикус, высоту стояния твердого неба, величину и вид небных миндалин, голосовую функцию (звучность голоса);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;3) осмотр и пальпация области щитовидной железы;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;4) пальпация периферических лимфоузлов: подчелюстных, паховых, подмышечных;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;5) исследование органов кровообращения (осмотр, аускультация сердца), определение частоты, наполнения, ритма пульса. При аускультации отмечают звучность и чистоту тонов. В случаях выявления сердечных шумов проводят исследования в различных положениях (стоя, лежа) и функциональные пробы с дозированной физической нагрузкой;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;6) исследование органов дыхания (аускультация легких);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;7) исследование органов пищеварения (пальпация органов брюшной полости, области правого подреберья, печени, эпигастральной области, селезенки, правой и левой подвздошной областей, надлобковой области). Обращается внимание на состояние слизистых, обложенность языка, десен, верхнего неба, зубов. Выявляются симптомы хронической интоксикации (бледность кожных покровов, орбитальные тени);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;8) детям в возрасте от трех до четырех месяцев, по показаниям ультразвуковое исследование тазобедренных суставов с целью раннего выявления дисплазии тазобедренных суставов и врожденного вывиха бедра;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;9) осмотр половых органов. При осмотре девочек старше 10 лет особое внимание уделяют гинекологическому анамнезу, жалобам, нарушению менструальной функции;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;10) оценка нервно-психического развития (далее – НПР) с рождения до 17 лет включительно с определением варианта группы развития: 1 – нормальный уровень НПР; 2 – незначительные отклонения в НПР; 3 – выраженные отклонения в НПР.</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;У детей до 3 лет оценка НПР проводится в соответствии с приказом Министра здравоохранения Республики Казахстан от 9 сентября 2010 года № 704 "Об утверждении Правил организации скрининга" (зарегистрирован в Реестре государственной регистрации нормативных правовых актов за № 6490).</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;В 4-6 лет оцениваются мышление и речь, моторное развитие, внимание и память, социальные контакты.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;В 7-8 лет: психомоторная сфера и поведение, интеллектуальное развитие, эмоционально-вегетативная сфера.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;В 9-10 лет дополнительно оценивается сформированность абстрактно-логических операций, логических суждений.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;В 11-17 лет определяются: эмоционально-вегетативная сфера, сомато-вегетативные проявления, вегето-диэнцефальные проявления;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;11) оценка полового развития методом пубертограмм, выдается заключение (норма, опережение, отставание);</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;12) выявление признаков жестокого обращения путем выявления нехарактерных для данного возраста травм и их последствий, признаков побоев (линейные кровоподтеки после ударов палкой или прутом, кровоподтеки в виде петли после ударов ремнем, веревкой, следы связывания, стягивания веревкой или ремнем, следы прижиганий сигаретой, кровоизлияния в сетчатку глаз, субдуральные гематомы).&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;Специализированный этап проводится врачами-специалистами и включает в себя осмотр с заполнением учетно-отчетной документации:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) хирург-ортопед у детей раннего возраста обращает внимание на наличие расширения пупочного, паховых колец, грыжевых выпячиваний в области белой линии живота, крипторхизма, выпадения прямой кишки, деформацию конечностей, грудной клетки. У детей дошкольного и школьного возраста определяется наличие деформации позвоночника, нарушений осанки, функций крупных и мелких суставов. Проводится осмотр состояния сводов стоп, оценка плантограммы, оценка походки. У мальчиков определяется опущение яичек;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) уролог исключает патологию мочеполовой системы;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;3) отоларинголог проводит переднюю риноскопию и проверку дыхательной функции носа, заднюю риноскопию, фарингоскопию, пальпацию шейных лимфоузлов (подчелюстных, передних и задних шейных, заушных), отоскопию, исследование слуха;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;4) невролог проводит общий осмотр (определение наличия дермографических черт, сосудистого рисунка), исследование состояния черепно-мозговых нервов, двигательных функций; сухожильных, периостальных, кожных рефлексов; оценку вегетативной регуляции;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;5) стоматолог проводит комплексное обследование ребенка с оценкой состояния прикуса, пародонта, индексов гигиены, интенсивности поражения зубов кариесом (КПУ - сумма кариозных пломбированных и удаленных зубов у одного ребенка), степени активности кариеса, гингивита, (РМА - папиллярно-маргинально-альвеолярный индекс для оценки воспалительного процесса десен), плана диспансеризации, реабилитации и профилактики;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;6) офтальмолог проводит определение остроты зрения, внешний осмотр органа зрения; осмотр с боковым освещением и в проходящем свете, офтальмоскопию;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;7) эндокринолог проводит осмотр для исключения гинекомастии, задержки или преждевременного полового развития, задержки роста, ожирения, увеличения щитовидной железы, сахарного диабета, крипторхизма.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;16. По окончании скринингового осмотра врачом педиатром или врачом общей практики, с учетом заключения профильных специалистов и лабораторно-диагностических исследований, проводится комплексная оценка состояния здоровья детей с определением следующих "групп здоровья":</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1 группа – здоровые дети;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2 группа – здоровые дети, имеющие функциональные отклонения, а также сниженную сопротивляемость к острым и хроническим заболеваниям, с наличием факторов риска;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;3 группа – дети, больные хроническими заболеваниями в состоянии компенсации, с сохраненными функциональными возможностями организма;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;4 группа – дети с хроническими заболеваниями в состоянии субкомпенсации, со сниженными функциональными возможностями;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;5 группа – дети с хроническими заболеваниями в состоянии декомпенсации, со значительно сниженными функциональными возможностями организма.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;17. Дети, относящиеся к 2, 3, 4, 5 группам здоровья подлежат динамическому наблюдению и оздоровлению у специалистов ПМСП или специалистов соответствующего профиля.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;18. Заключительный этап – дообследование и постановка на диспансерный учет лиц с выявленной патологией, завершение оформления учетно-отчетной статистической документации.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;19. По завершении скринингового осмотра проводится комплексная оценка здоровья, оформляются эпикриз и заключение с указанием группы здоровья, оценкой физического и нервно-психического развития. В заключении даются рекомендации по дообследованию, наблюдению, соблюдению санитарно-гигиенических правил, режиму, физическому воспитанию и закаливанию, проведению профилактических прививок, профилактике пограничных состояний и заболеваний, по физкультурной группе (основная или специальная группа).</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;20. На заключительном этапе врач организации ПМСП или ответственное лицо организации ПМСП вносит результаты скринингового осмотра в статистическую карту профилактического медицинского осмотра (скрининга) ребенка по форме № 025-07/у, утвержденной Приказом № 907, в историю развития ребенка (формы №112/у-д, формы №112/у-м утвержденные Приказом № 907), в паспорт здоровья, а также в Автоматизированную информационную систему "Поликлиника".</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;21. Результаты скринингового осмотра детей (копия эпикриза и заключения) доводятся до сведения родителей или их законных представителей.</p>
        <p><strong>&nbsp;Глава 2. Порядок проведения скринингового осмотра взрослого населения</strong></p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;22. Проведение скрининговых осмотров целевых групп взрослого населения включает в себя подготовительный этап, скрининг и заключительный этап.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;23. Подготовительный этап состоит из мероприятий по формированию целевых групп, информационного сопровождения и приглашения на скрининг.</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;Подготовительный этап осуществляется средним медицинским персоналом медицинского пункта, фельдшерско-акушерского пункта, районной, городской поликлиники организации ПМСП и включает в себя:&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;ежегодное формирование и составление списка целевых групп, подлежащих скрининговым осмотрам в предстоящем году, до 15 ноября календарного года с последующей ежемесячной коррекцией целевых групп;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;оповещение целевых групп взрослого населения о необходимости и условиях прохождения скрининговых осмотров;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;приглашение целевой группы населения на скрининговый осмотр;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;обеспечение своевременного прохождения скринингового осмотра целевыми группами населения.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;24. При прохождении скринингового осмотра на ранее выявления рака шейки матки, дополнительно на подготовительном этапе средний медицинский персонал организации ПМСП или ответственное лицо организации ПМСП информирует пациентку о требованиях к прохождению скрининга: исключить половые сношения, влагалищные манипуляции, включая спринцевание, ванночки, тампоны за сутки до осмотра.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;25. При прохождении скринингового осмотра на ранее выявления колоректального рака, дополнительно на подготовительном этапе средний медицинский персонал организации ПМСП или ответственное лицо организации ПМСП информирует пациентов целевой группы о требованиях к прохождению скрининга: исследование не проводится в период менструации, при наличии кровоточащего геморроя, крови в моче или после значительных усилий при дефекации.</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;26. Этап скрининга скрининговых осмотров включает в себя: проведение осмотра, исследований и заполнение статистических карт профилактического медицинского осмотра (скрининга) амбулаторного пациента по форме № 025-08/у, утвержденной Приказом № 907.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;27. Скрининг на раннее выявление артериальной гипертонии, ишемической болезни сердца и сахарного диабета состоит из двух этапов:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) первый этап проводится средним медицинским персоналом организации ПМСП или ответственным лицом организации ПМСП, предназначен для всей целевой группы и включает в себя:</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;проведение антропометрических измерений (вес, рост, объем талии), вычисление индекса Кетле;&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;проведение опроса по скрининг-тесту (пункты 11-24, 31-33, формы № 025-08/у, утвержденной Приказом № 907);</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;двукратное измерение артериального давления (далее – АД) с интервалом в 1-2 минуты в положении сидя. Перед измерением АД больной должен посидеть 3-5 минут;&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;проведение экспресс-диагностики общего холестерина и глюкозы крови&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;(при отсутствии такового оборудования, биоматериал (кровь) пациента направляется в лабораторию).</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) на втором этапе врач ПМСП или ответственное лицо организации ПМСП использует шкалу&nbsp;SCORE&nbsp;для определения сердечно-сосудистого риска и принятия решения о дальнейшем ведении скринируемого лица:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;лицам с низким сердечно-сосудистым риском (до 1% по шкале&nbsp;SCORE) рекомендует проведение последующего скринингового осмотра через 2 года, дает рекомендации по ведению здорового образа жизни с целью сохранения низкого сердечно-сосудистого риска;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;лиц с умеренным сердечно-сосудистым риском (<u>&gt;</u> 1% до &lt; 5% по шкале&nbsp;SCORE) направляет в школу по профилью заболевания (далее - школа здоровья) с целью снижения или стабилизации сердечно-сосудистого риска;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;лиц с высоким и очень высоким сердечно-сосудистым риском (<u>&gt;</u> 5% по шкале&nbsp;SCORE) направляет на электрокардиографию и, по показаниям, на консультацию кардиолога. При выявлении у пациента болезней системы кровообращения (далее – БСК) ставит на диспансерный учет, при отсутствии БСК направляет в школу здоровья с целью снижения сердечно-сосудистого риска;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;при диагностике факторов риска за уровень гиперхолестеринемии принимается уровень <u>&gt;</u> 5 ммоль/л;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;при оценке сердечно-сосудистого риска определяется частота сердечных сокращений обследуемого;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;при выявлении повышенного уровня глюкозы крови направляет пациента на консультацию участкового терапевта, врача общей практики, эндокринолога.</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;28. Кардиолог, эндокринолог консультативно-диагностического отделения районной, городской поликлиники (далее – КДО) или консультативно-диагностического центра (далее – КДЦ) проводит последующее обследование согласно протоколов диагностики и лечения, по показаниям направляет в специализированные медицинские организации (кардиологический, эндокринологический).&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;29. На этапе скрининга на раннее выявления глаукомы средний медицинский персонал организации ПМСП или ответственное лицо организации ПМСП:</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;проводит опрос по скрининг-тесту (пункты 22-28,34,35 формы № 025-08/у, утвержденной Приказом № 907);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;измеряет внутриглазное давление по Маклакову или с использованием бесконтактного тонометра.</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;30. При положительных ответах на скрининг-тест (пункты 23-26 формы&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;№ 025-08/у, утвержденной Приказом № 907), и (или) повышенном внутриглазном давлении (свыше 25 мм ртутного столба при тонометрии по Маклакову и свыше 20 мм ртутного столба при бесконтактной тонометрии), а также при разнице в показателях внутриглазного давления на правом и левом глазу более 5 мм ртутного столба направляет пациента к офтальмологу КДО,&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;КДЦ.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;31. Офтальмолог КДО, КДЦ проводит дообследование, по показаниям направляет в глаукомный кабинет.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;32. На этапе скрининга на раннее выявление поведенческих факторов риска (избыточная масса тела, ожирение, физическая активность, курение, потребление алкоголя в опасных дозах) исследуются все лица, прошедшие скрининг на раннее выявление артериальной гипертонии, ишемической болезни сердца и сахарного диабета, глаукомы, онкопатологии.</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;33. Выявление факторов риска проводится средним медицинским персоналом организации ПМСП или ответственным лицом организации ПМСП и включает в себя проведение опроса по скрининг-тесту (пункты 11-28, 31-35, формы № 025-08/у, утвержденной Приказом № 907).</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;34. Врач (средний медицинский персонал) организации ПМСП или ответственное лицо организации ПМСП:</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;проводит профилактическую консультацию и предоставляет подробные рекомендации по изменению поведения;&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;направляет в профильную школу здоровья.&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;35. Этап скрининга на раннее выявление рака шейки матки включает:&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) цитологическое исследование мазка из шейки матки с окраской по Папаниколау (Рар-тест), которое проводится традиционным методом или с использованием метода жидкостной цитологии с интерпретацией по Терминологической системе Бетесда, 2001 (далее – ТСБ);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) углубленную диагностику (кольпоскопию, биопсию, гистологическое исследование) при цитологических заключениях по ТСБ: атипичные клетки плоского эпителия, не позволяющие исключить высокую степень плоскоклеточного интраэпителиального поражения (далее -&nbsp;ASC-H), высокая степень плоскоклеточного интраэпителиального поражения (далее –&nbsp;HSIL), атипические железистые клетки (далее –&nbsp;AGС), аденокарцинома&nbsp;in&nbsp;situ&nbsp;(далее –&nbsp;AIS), рак.</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;36. Забор материала на цитологическое исследование осуществляется в смотровом кабинете организации ПМСП.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;37. Средний медицинский персонал организации ПМСП или ответственное лицо организации ПМСП:</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;заполняет журнал учета пациентов, подлежащих цитологическому скринингу, вносит данные в форму № 025-08/у, утвержденную Приказом № 907;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;направляет в смотровой кабинет организации ПМСП.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;38. Акушер смотрового кабинета организации ПМСП:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) осматривает шейку матки в зеркалах;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;2) осуществляет взятие мазков на цитологическое исследование (Рар-тест) традиционным методом (одно стекло) или методом жидкостной цитологии (один контейнер);&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;3) заполняет направление на цитологическое исследование и направляет биоматериал в цитологическую лабораторию;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;4) осуществляет контроль за получением результатов Рар-теста из цитологической лаборатории.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;Срок получения результатов и информирования пациентов о результатах скрининга не должен превышать двух недель.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;39. Цитологическое исследование проводится в цитологической лаборатории с пропускной способностью не менее 15000 скрининговых исследований ежегодно, специалисты которой (врач-цитолог, цитотехник) регулярно проходят специальную подготовку (обучение на кафедре постдипломной подготовки по онкоцитологии).</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;Регистратор цитологической лаборатории, цитотехник:&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;регистрирует поступившие стекла, контейнеры в журнале цитологических исследований, проводимых по скринингу;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;направляет результаты цитологического исследования в организацию ПМСП.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;40. Углубленная диагностика (кольпоскопия, биопсия) проводится по показаниям при цитологическом заключении&nbsp;ASC-H,&nbsp;HSIL,&nbsp;AGС,&nbsp;AIS, рак онкогинекологом онкологического центра или онкологического отделения в структуре многопрофильной больницы (далее - ОЦ) или гинекологом районной, городской поликлиники, прошедшим специальную подготовку (обучение).</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;Наличие эрозии без подтверждающего цитологического результата не является показанием для кольпоскопии во время скрининга.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;Гистологическое исследование проводится в лаборатории патоморфологии или патологоанатомического бюро. Морфологическая интерпретация биоптата осуществляется с рекомендациями Всемирной организации здравоохранения.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;41. Врач организации ПМСП или ответственное лицо организации ПМСП при получении результатов Рар-теста по ТСБ:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;плохое качество мазка приглашает пациентку на цитологический скрининг повторно;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;"Цитограмма без особенностей" (норма) рекомендует пациентке проведение последующего цитологического скрининга через 4 года;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;"Воспалительный процесс или микроорганизмы" рекомендует лечение и санацию у врача гинеколога;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;"Атипия эпителиальных клеток неопределенного значения" (далее -&nbsp;ASC-US), "низкая степень плоскоклеточного интраэпителиального поражения" (далее -&nbsp;LSIL) рекомендует лечение и динамическое наблюдение у врача-гинеколога районной, городской поликлиники с проведением цитологического контроля через 6 месяцев. При персистенции или утяжелении рекомендуется проведение кольпоскопии с биопсией, при нормальной цитограмме через 6 месяцев – дополнительный Рар-тест через 12 месяцев;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;"высокая степень плоскоклеточного интраэпителиального поражения" (далее –&nbsp;HSIL), "атипичные клетки плоского эпителия, не позволяющие исключить&nbsp;HSIL" (далее - АSC-H), "атипические железистые клетки" (далее –&nbsp;AGС), "аденокарцинома ин ситу канала шейки матки" (далее –&nbsp;AIS), рак направляет пациентку на углубленную диагностику к онкогинекологу ОЦ или гинекологу районной, городской поликлиники, прошедшему специальную подготовку.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;42. При получении гистологических результатов биопсии шейки матки:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;при цервикальной интраэпителиальной неоплазии первой степени (далее –&nbsp;CIN&nbsp;1) рекомендует пациентке дообследование, лечение и динамическое наблюдение у гинеколога районной, городской поликлиники с проведением цитологического контроля через 6 и 12 месяцев;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;при цервикальной интраэпителиальной неоплазии второй степени(далее –&nbsp;CIN&nbsp;2),цервикальной интраэпителиальной неоплазиитретьей степени (далее –&nbsp;CIN3), карцинома ин ситу (далее -&nbsp;CIS), направляет пациентку в ОЦ для лечения и динамического наблюдения;&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;все пациентки после лечения&nbsp;CIN&nbsp;2,&nbsp;CIN&nbsp;3,&nbsp;CIS,&nbsp;AIS&nbsp;наблюдаются у гинеколога районной, городской поликлиники с проведением контрольных Рар-тестов через 4, 6 и 12 месяцев. По показаниям (положительный Рар-тест в контроле) проводится консультация онкогинеколога ОЦ.&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;43. Бланк результата цитологического исследования вклеивается в медицинскую карту амбулаторного больного.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;44. Этап скрининга на раннее выявление рака молочной железы включает:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) проведение маммографии обеих молочных желез в двух проекциях – прямой и косой в кабинете маммографии городской, районной поликлиники (передвижного медицинского комплекса). Все цифровые маммограммы распечатываются на рентгенографической пленке в масштабе 1:1 – 100% (1 пациентка – 1 комплект – 2 или 4 маммограммы) и копируются на цифровой носитель&nbsp;CD,&nbsp;DVD, при наличии системы архивирования и передачи медицинских изображений передаются на сервер кабинета маммографии ОЦ;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) интерпретацию маммограмм по классификации&nbsp;BI-RADS&nbsp;(М0, М1, М2, М3, М4, М5) двумя и более независимыми врачами-рентгенологами одной медицинской организации (ОЦ) – двойная читка, или разными медицинскими организациями: врачом-рентгенологом кабинета маммографии городской, районной поликлиники (передвижного медицинского комплекса) – первая читка, и врачом-рентгенологом кабинета маммографии ОЦ – вторая читка;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;3) углубленную диагностику – прицельная маммография, ультразвуковое исследование (далее – УЗИ) молочных желез, пункционная биопсия или стереотаксическая пункция для гистологического исследования, которая проводится в случае выявления патологических изменений на маммограммах (М4, М5 по классификации&nbsp;BI-RADS) в кабинете маммографии ОЦ.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;45. Средний медицинский персонал организации ПМСП или ответственное лицо организации ПМСП направляет пациентку на маммографию в районную, городскую поликлиники.</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;46. Рентгенлаборант кабинета маммографии городской, районной поликлиники (передвижного медицинского комплекса):&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;Заполняет направление на двойную читку маммограмм, журнал учета пациентов, подлежащих маммографическому скринингу, утвержденные Приказом № 907;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;при проведении первой читки – вносит результаты первой читки в форму № 025-08/у, утвержденную Приказом № 907 и в направление маммограмм на двойную (вторую) читку;&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;при получении результатов двойной (второй) читки – вносит результаты двойной (второй) читки в форму № 025-08/у, утвержденную Приказом № 907 и в журнал учета пациентов, подлежащих маммографическому скринингу.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;47. Врач-рентгенолог кабинета маммографии городской, районной поликлиники (передвижного медицинского комплекса):</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;выполняет требования к безопасности и качеству маммографических исследований;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;оценивает качество предоставленных изображений и правильность укладки;&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;проводит первую читку маммограмм с интерпретацией результатов по классификации&nbsp;BI-RADS;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;направляет маммограммы, электронные копии скрининговых маммограмм посредством системы архивирования и передачи медицинских изображений на сервер кабинета маммографии ОЦ вместе с направлениями на двойную читку маммограмм.</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;48. Врач-рентгенолог кабинета маммографии ОЦ:&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;оценивает качество предоставленных изображений и правильность укладки;&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;проводит двойную (вторую) читку маммограмм с интерпретацией результатов по классификации&nbsp;BI-RADS. По показаниям организует третью читку;&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;направляет результаты двойной (второй) читки (отрывную часть направления) в организацию ПМСП;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;рекомендует организации ПМСП по показаниям пригласить пациентку на углубленную диагностику (прицельную маммографию, УЗИ молочных желез, пункционную биопсию или стереотаксическую пункцию с последующим гистологическим исследованием материала);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;осуществляет сбор и архивирование всех маммограмм (пленки и электронные носители), сделанных в рамках скрининга. Срок хранения маммограмм – не менее 3 лет после выхода из скринингового возраста. Маммограммы, сделанные во время скрининга, пациентке не выдаются.</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;49. Показаниями для углубленной диагностики являются заключения двойной читки маммограмм М4 (признаки, вызывающие подозрение на злокачественность), М5 (практически достоверные признаки злокачественности).&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;50. Врач организации ПМСП или ответственное лицо организации ПМСП при получении результата маммографии по классификации&nbsp;BI-RADS:</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;при М0 (необходимы дополнительные изображения либо данные предыдущего исследования) – направляет пациентку на повторное рентгенологическое исследование в кабинет маммографии городской, районной поликлиники (передвижного медицинского комплекса) либо выполняет рекомендации врача-рентгенолога кабинета маммографии ОЦ по результатам сопоставления с архивированными снимками (при их наличии);&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;при М1 (изменений не выявлено) – рекомендует пациентке проведение последующего скринингового маммографического обследования через 2 года;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;при М2 (доброкачественные изменения) направляет пациентку на консультацию к онкологу (маммологу) КДО, КДЦ с последующим прохождением скринингового маммографического обследования через 2 года;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;при М3 (вероятные доброкачественные изменения) – направляет пациентку на краткосрочное динамическое наблюдение к участковому врачу или врачу общей практики с рекомендацией контрольной маммографии через 6 месяцев;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;при М4 (признаки, вызывающие подозрение на злокачественность), М5 (практически достоверные признаки злокачественности) – в соответствии с результатами углубленной диагностики направляет пациентку на консультацию и динамическое наблюдение к онкологу, маммологу ОЦ. В случае исключения злокачественного новообразования пациентка с указанными результатами маммографии направляется на консультацию к онкологу (маммологу) КДО, КДЦ с последующим динамическим наблюдением и оздоровлением у специалистов ПМСП по месту ее прикрепления;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;вклеивает отрывную часть направления маммограмм на двойную (вторую) читку с результатами и заключением врача-рентгенолога кабинета маммографии ОЦ в медицинскую карту амбулаторного пациента.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;51. Этап скрининга на раннее выявление колоректального рака включает в себя:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) проведение гемокульт-теста, основанный на иммунохимическом или иммунохроматографическом методе, который проводится в домашних условиях после получения теста и разъяснения правил проведения исследования;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;2) тотальную колоноскопию при положительном гемокульт-тесте с биопсией патологических участков слизистой кишечника, которая проводится в случае их выявления. Тотальная колоноскопия проводится в отделениях (кабинетах) колоноскопии, оснащенных видеоэндоскопическим оборудованием с поддержкой узкоспектральной эндоскопии, возможностью видеофиксации исследований, хирургическим инструментарием для взятия биопсии и проведения малоинвазивных хирургических вмешательств (удаление полипов на тонкой ножке), аппаратом автоматической обработки эндоскопического оборудования предпочтительно со встроенной функцией печати времени обработки эндоскопа.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;52. Средний медицинский персонал ПМСП для проведения гемокульт-теста разъясняет, что для исследования используется небольшая часть стула, помещенного в чистой, сухой емкости; забор материала проводится с использованием наконечника контейнера путем введения в разные места стула; после забора материала крышка закручивается, контейнер несколько раз встряхивается; 3 капли раствора со взвешенными частицами кала наносятся в специальное окошко тест-карты; интерпретация теста проводится через 3-10 минут (или как указано производителем теста). По показаниям тест проводится в условиях организации ПМСП.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;53. Оценка результата теста проводится самим пациентом, если тест проводится в домашних условиях, либо медицинским работником в поликлинике:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;в случае появление двух полосок на уровне Т (тест) и С (контроль) расценивается как положительный результат (наличие крови в кале);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;появление одной полоски на уровне С (контроль) расценивается как отрицательный результат (отсутствие крови в кале);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;появление одной полоски на уровне Т (тест) расценивается как ошибочный результат;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;отсутствие полоски на уровне С (контроль) расценивается как ошибочный результат.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;54. Если тест проводится в домашних условиях, необходимо попросить пациента или его родственников воспроизвести на бумаге результаты теста с указанием полосок на уровнях Т и С или сфотографировать на камеру, телефон для подтверждения достоверности результата.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;55. При получении результатов гемокульт-теста средний медицинский персонал организации ПМСП или ответственное лицо организации ПМСП:</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;проводит опрос по скрининг-тесту (пункты 29,38,43,44), заполняет форму № 025-08/у, утвержденную Приказом № 907;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;оценивает результат гемокульт-теста (отрицательный, положительный).</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;В случае некорректного результата тест следует повторить.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;Положительный результат гемокульт-теста необходимо подтвердить врачом организации ПМСП или ответственным лицом организации ПМСП.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;Результаты теста доводятся до пациента и среднего медицинского персонала организации ПМСП или ответственного лица организации ПМСП в течение 3 рабочих дней после дня их получения.</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;56. В случае положительного гемокульт-теста пациент направляется на обследование всего отдела толстого кишечника (далее – тотальная колоноскопия).&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;57. Врач организации ПМСП или ответственное лицо организации ПМСП:</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;вносит результаты гемокульт-теста в журнал учета пациентов, подлежащих колоректальному скринингу;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;информирует пациента о необходимости проведения исследования, методике исследования, подготовке к колоноскопии;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;направляет на тотальную колоноскопию, которая проводится при положительном гемокульт-тесте.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;58. Врач отделения эндоскопии:</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;проводит биопсию образований слизистой по показаниям. Гистологическое исследование биоптата слизистой толстой кишки производится в патоморфологической лаборатории или патологоанатомическом бюро. Морфологическая интерпретация биоптата осуществляется в соответствии с рекомендациями Всемирной организации здравоохранения;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;проводит одномоментную полипэктомию с клипированием или электрокоагуляцией при выявлении полипов на тонкой ножке при наличии соответствующего инструментария либо "холодную" биопсию при полипах размерами до 0,5 см;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;вносит результаты (непосредственные после колоноскопии и после получения гистологического заключения) в бланк колоноскопического исследования;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;осуществляет комплекс санитарно-гигиенических мероприятий по безопасному проведению эндоскопических процедур с целью исключения инфицирования лиц, проходящих эндоскопическое исследование.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;59. В случае использования одного эндоскопического аппарата интервал между предыдущим и последующим эндоскопическими исследованиями составляет не менее 20 минут с учетом проведения очистки, дезинфекции высокого уровня, промывки и продувки эндоскопа с использованием аппарата автоматической обработки эндоскопического оборудования.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;60. Медицинская сестра отделения эндоскопии:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;регистрирует проведение исследования в журнале регистрации эндоскопических исследований толстой кишки, выполненных во время скрининга, с указанием даты проведения исследования, фамилии, имени, отчества (при его наличии) пациента, возраста, адреса проживания;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;маркирует, регистрирует и направляет взятый биопсийный материал в патоморфологическую лабораторию или патологоанатомическое бюро в соответствии с установленными правилами с указанием на сопроводительном бланке отметки "Материал взят по скринингу";</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;направляет результаты эндоскопического исследования в организацию ПМСП.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;61. Врач организации ПМСП или ответственное лицо организации ПМСП:</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;1) при получении результатов гемокульт-теста:&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;отрицательного (отсутствия скрытой крови в кале) рекомендует проведение скрининга через 2 года;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;положительного (наличие скрытой крови в кале), но при отказе пациента от колоноскопии, подписанного пациентом или его законными представителями и вклеенного в медицинскую карту амбулаторного пациента, или наличии медицинских противопоказаний к проведению эндоскопического исследования, проводит беседу с пациентом, включает в группу риска с контрольным проведением гемокульт-теста, направляет на консультацию к хирургу или колопроктологу КДО, КДЦ.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) при получении результатов эндоскопического исследования:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;без патологии (далее – СS&nbsp;1) обследованным лицам рекомендует проведение гемокульт-теста в рамках скрининга через 2 года;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;наследственные заболевания толстой кишки и аномалии ее развития, хронические воспалительные заболевания кишки (далее – СS&nbsp;2-3) направляет к гастроэнтерологу или хирургу или колопроктологу КДО, КДЦ;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;полиповидные образования (далее – СS&nbsp;4) рекомендует лечение (полипэктомия), динамическое наблюдение у онколога, хирурга или колопроктолога КДО, КДЦ с контрольным проведением колоноскопии через 4-6 месяцев после полипэктомии;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;злокачественное новообразование толстой кишки с морфологической верификацией или без (далее – СS&nbsp;5-8) направляет в ОЦ, с последующим динамическим наблюдением у онколога по месту жительства.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;62. Заключительный этап скринингового осмотра включает в себя дообследование и постановка на диспансерный учет лиц с выявленной патологией, завершение оформления учетно-отчетной статистической документации. На данном этапе врач организации ПМСП или ответственное лицо организации ПМСП вносит результаты скрининга взрослого населения с выделением факторов риска (поведенческих, биологических), заключительного диагноза в форму № 025-08/у, медицинскую карту амбулаторного пациента, а также в Автоматизированную информационную систему "Поликлиника", дает рекомендации по дообследованию, наблюдению.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;63. По окончании скринингового осмотра врач организации ПМСП или ответственное лицо организации ПМСП с учетом заключения профильных специалистов и лабораторно-диагностических исследований определяет следующие группы диспансерного наблюдения:</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;1) 1А группа – здоровые без факторов риска: лица, не предъявляющие никаких жалоб и у которых в анамнезе и во время осмотра не выявлены хронические заболевания, факторы риска или нарушения функций отдельных органов и систем;&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;2) 1Б группа – здоровые с факторами риска: лица с выявленными факторами риска, пограничными состояниями, нуждающиеся в наблюдении и профилактическом вмешательстве (лица, у которых выявлены незначительные отклонения от установленных границ нормы в величинах артериального давления и прочих физиологических характеристик, не влияющие на функциональную деятельность организма);&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;3) 2 группа – практически здоровые: лица, имеющие в анамнезе острое и хроническое заболевание без обострений в течение последних 2 лет;&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;4) 3 группа– больные: лица, нуждающиеся в динамическом наблюдении, лечении и оздоровлении.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;64. Здоровые (1А, 1Б группы) и практически здоровые (2 группа) лица взрослого населения проходят:</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;последующий скрининговый осмотр в организации ПМСП в соответствии с периодичностью согласно приложению 2 к настоящим Правилам;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;оздоровление в кабинете здорового образа жизни, школе по профилю заболеваний организаций ПМСП и службы здорового образа жизни.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;65. Выявленные в ходе скрининговых осмотров лица с хроническими заболеваниями и (или) перенесшие отдельные острые заболевания, подлежат диспансерному учету и динамическому наблюдению с последующим оздоровлением и проведением комплекса лечебно-профилактических мероприятий, направленных на восстановление и укрепление здоровья.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;66. Каждый участник скринингового осмотра информируется о результатах обследования с выдачей соответствующих рекомендаций.</p>
        <p><strong>&nbsp;Раздел 3. Периодичность проведения скрининговых осмотров целевых групп населения</strong></p>
