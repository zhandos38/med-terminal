        <p>&nbsp; &nbsp; &nbsp; 30. Скрининг - тестке (№ 907 бұйрықпен бекітілген № 025-08/е нысанның 23-26-тармақтары) оң жауаптар және (немесе) жоғары көзішілік қысым болған жағдайда (Маклаков бойынша тонометрде 25 мм сынап бағанасы жоғары болса және жанаспайтын тонометрде 20 мм сынап бағанасы жоғары болса), сондай-ақ сол жақ және оң жақ көздің көзішілік қысым көрсеткіштерінде 5 мм.сынап бағасының артық айырмашылықтар болған жағдайда пациентті КДБ,КДО офтальмологына жібереді.</p>
        <p>&nbsp; &nbsp; &nbsp; 31. КДБ, КДО офтальмологы толық қарап-тексеруді жүргізеді, қажет болса глаукома кабинетіне жібереді.</p>
        <p>&nbsp; &nbsp; &nbsp; 32. Мінез-құлық қауіп факторларын (дененің артық салмағы, дене қимылының белсенділігі, шылым шегу, қауіпті дозаларда ішімдікті пайдалану) ерте анықтауға скрининг сатысы артериялық гипертония, жүректің ишемиялық ауру, қант диабеті, глаукома, онкологиялық патология ерте анықтауға скринингтен өткен адамдардың арасында зерттеледі.</p>
        <p>&nbsp; &nbsp; &nbsp; 33. Қауіп факторларын анықтау МСАК ұйымдарының орта медицина қызметкері немесе МСАК ұйымдарының жауапты адамы жүргізеді және -тест бойынша сауалнамалар жүргізуді (№ 907 бұйрықпен бекітілген № 025-08/е нысанның 11-28,31-35 тармақтары) қамтиды.</p>
        <p>&nbsp; &nbsp; &nbsp; 34. МСАК ұйымдарының дәрігері (орта медицина қызметкері) немесе МСАК ұйымдарының жауапты адамы:</p>
        <p>&nbsp; &nbsp; &nbsp; профилактикалық консультациялар жүргізеді және мінез-құлқының өзгеруі бойынша толық ұсынымдар ұсынады;</p>
        <p>&nbsp; &nbsp; &nbsp; бейінді денсаулық мектебіне жібереді.</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;35. Жатыр мойны обырын ерте анықтаудың скрининг сатысы мыналарды қамтиды:&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; 1) Папаниколау (Пап-тест) бойынша жатыр мойнынан жағындыларды дәстүрлі әдіспен және Бетесда терминологиялық жүйесі (БТЖ) 2001 бойынша талдаумен сұйықтық цитологиялық әдісін қолданумен зерттеу;</p>
        <p>&nbsp; &nbsp; &nbsp; 2) БТЖ бойынша жоғары деңгейлі жалпақжасушалы эпителий ішілік бұзылуды жоққа шығармайтын жалпақ эпителийдің атпиялық жасушалары ( бұдан әрі - ASC-H), жалпақ эпителий ішілік бұзылудың жоғары деңгейі (бұдан әрі – HSIL), атипиялық безді жасушалар (бұдан әрі – AGS), аденокарцинома ин ситу(бұдан әрі – AIS), обыр цитологиялық қорытындылар кезінде тереңдетілген диагностикалау (кольпоскопия, биопсия, гистологиялық зерттеу).</p>
        <p>&nbsp; &nbsp; &nbsp; 36. Цитологиялық тексеріп-қарауға материал алу МСАК ұйымдарының қарап-тексеру кабинетінде іске асырылады.</p>
        <p>&nbsp; &nbsp; &nbsp; 37. МСАК ұйымдарының орта медицина қызметкері немесе МСАК ұйымдарының жауапты адамы :</p>
        <p>&nbsp; &nbsp; &nbsp; цитологиялық скринингке жататын пациенттерді есепке алу журналын толтырады, № 907 бұйрықпен бекітілген № 025-08/е нысанды толтырады;</p>
        <p>&nbsp; &nbsp; &nbsp; МСАК ұйымдарының қарап-тексеру кабинеттеріне жібереді.</p>
        <p>&nbsp; &nbsp; &nbsp; 38. МСАК ұйымдарының қарап-тексеру кабинетінің акушері:</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;1) жатыр мойнын айналармен қарайды;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; 2) цитологиялық зерттеуге (Рар-тест) дәстүрлі әдіспен (бір шыны) немесе сұйықтық цитологиялық әдісімен (бір контейнер) жағындыларды алуды жүзеге асырады;</p>
        <p>&nbsp; &nbsp; &nbsp; 3) цитологиялық тексеріп-қарауға жолдаманы толтырады және биоматериалды цитологиялық зертханаға жібереді;</p>
        <p>&nbsp; &nbsp; &nbsp; 4) Рар-тест нәтижелерін цитологиялық зертханадан алуды бақылауын іске асырады.</p>
        <p>&nbsp; &nbsp; &nbsp; Нәтижелерді алу мерзімі мен скрининг нәтижелері туралы әйелдерді ақпараттандыру екі аптадан аспауы керек.</p>
        <p>&nbsp; &nbsp; &nbsp; 39. Цитологиялық зерттеу, жыл сайын кемінде 15000 скринингтік зерттеудің өткізу қабілетімен цитологиялық зертханада жүргізіледі, осы цитологиялық зертхананың мамандары (цитолог-дәрігер, цитотехник) үнемі арнайы дайындықтан (онкоцитология бойынша дипломнан кейінгі дайындық кафедрасында білім алу) өтеді.</p>
        <p>&nbsp; &nbsp; &nbsp; Цитологиялық зертхананың тіркеушісі/цитотехник:</p>
        <p>&nbsp; &nbsp; &nbsp; келіп түскен шынылар, құтыларды скрининг бойынша жүргізілетін цитологиялық зерттеулер журналында тіркейді;</p>
        <p>&nbsp; &nbsp; &nbsp; цитологиялық зерттеулердің нәтижелерін МСАК ұйымдарына жібереді.</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;40. Тереңдетілген диагностика (кольпоскопия, биопсия) ASC-H, HSIL, AGS, AIS, қатерлі ісік цитологиялық қорытындысы кезінде, онкологиялық орталықтың немесе көпбейінді аурухананың құрылымындағы онкологиялық бөлімнің (бұдан әрі – ОО) онкогинекологымен немесе арнайы дайындықтан (оқытудан) өткен қалалық, аудандық емханасының гинекологымен көрсетілімдер кезінде жүргізіледі.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; Скрининг кезінде цитологиялық дәлелденген нәтижесіз эрозияның болуы кольпоскопия үшін көрсеткіш болып табылмайды.</p>
        <p>&nbsp; &nbsp; &nbsp; Гистологиялық зерттеу патологоанатомиялық бюроның немесе патоморфология зертханасында жүргізіледі. Биоптаттың морфологиялық түсіндіруі Дүниежүзілік Денсаулық сақтау ұйымының ұсыныстарына сәйкес іске асырылады.</p>
        <p>&nbsp; &nbsp; &nbsp; 41. МСАК ұйымдарының дәрігері немесе МСАК ұйымдарының жауапты адамы БТЖ бойынша Рар-тестінің нәтижелерін алған кезде:</p>
        <p>&nbsp; &nbsp; &nbsp; жағынды сапасының нашар болуы пациентті цитологиялық скринингке қайтадан шақырады;</p>
        <p>&nbsp; &nbsp; &nbsp; "Цитограмма өзгеріссіз" (норма) пациентке келесі цитологиялық скринингті 4 жылдан кейін жүргізуді ұсынады;</p>
        <p>&nbsp; &nbsp; &nbsp; "Қабыну үдерісі немесе микроорганизмдер" дәрігер гинекологта емделу мен тазартуды ұсынады;</p>
        <p>&nbsp; &nbsp; &nbsp; "Маңызы анықталмаған эпителийлік жасушалардың атипиясы" (бұдан әрі -ASC-US), "жалпақжасушалы эпителий ішілік бұзылудың төменгі деңгейі" (бұдан әрі – LSIL) аудандық, қалалық емханалардың гинеколог-дәрігерінде цитологиялық бақылауды 6 айдан кейін жүргізумен емделу мен динамикалық бақылауды ұсынады. Персистенция немесе ауырлау кезінде биопсиямен қалыпты цитограммада 6 айдан кейін кольпоскопия жүргізуді – қосымша Рар-тест 12 айдан кейін ұсынады;</p>
        <p>&nbsp; &nbsp; &nbsp; жалпақ эпителий ішілік бұзылудың жоғары деңгейі (бұдан әрі – HSIL), жоғары деңгейлі жалпақжасушалы эпителий ішілік бұзылуды жоққа шығармайтын жалпақ эпителийдің атпиялық жасушалары ( бұдан әрі - ASC-H), атипиялық безді жасушалар (бұдан әрі – AGS), аденокарцинома ин ситу(бұдан әрі – AIS), обыры кезінде пациентті арнайы дайындықтан өткен аудандық, қалалық емханалардың гинекологына, ОО-тың онкогинекологына жібереді.</p>
        <p>&nbsp; &nbsp; &nbsp; 42. Жатыр мойнының биопсиясының гистологиялық нәтижелерін алу кезінде:</p>
        <p>&nbsp; &nbsp; &nbsp; бірінші деңгейлі цервикальдық эпителий ішілік неоплазия ( бұдан әрі - CIN 1) кезінде пациентке аудандық, қалалық емханалардың гинекологында 6 және 12 айдан кейін цитологиялық бақылауды жүргізумен толық зерттеп-қаралуды, емделуді және динамикалық байқауда болуды ұсынады;</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;екінші деңгейлі цервикальдық эпителий ішілік неоплазия ( бұдан әрі - CIN 2), үшінші деңгейлі цервикальдық эпителий ішілік неоплазия ( бұдан әрі - CIN 3), карцинома ин ситу (бұдан әрі – CIS), AIS, обыр кезінде пациентті емдеу және динамикалық байқау үшін ОО-қа жолдайды;</p>
        <p>&nbsp; &nbsp; &nbsp; CIN 2, CIN 3, CIS, AIS емделгеннен кейін барлық пациенттер аудандық, қалалық емханалардың гинекологында 4, 6 және 12 айдан кейін қайталама Рар-тестілерді жүргізумен бақыланады. Қажет болған жағдайда (оң Рар-тест бақылауда) ОО онкогинекологтарының консультациясын жүргізеді.</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;43. Цитологиялық тексеріп-қарау нәтижесінің бланкі амбулаториялық науқастың медициналық картасына жапсырылады.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; 44. Сүт безі обырын ерте анықтау скринингі сатысына кіреді:</p>
        <p>&nbsp; &nbsp; &nbsp; 1) қос сүтбезінің маммографиясы қалалық, аудандық емханалардың (жылжымалы медициналық кешеннің) маммографиялық кабинетінде түзу және қисық 2 проекцияда жүргізу. Барлық цифрлық маммограммалар 100% -1:1 көлемінде рентгенографиялық пленкаларда шығарылады (1 пациент – 1 жиынтық – 2 немесе 4 маммограммалар) және CD/DVD цифрлық тасымалдаушыға көшіріледі, архивтеу және медициналық суреттерді табыстау жүйесі болған жағдайда ОО маммография кабинеттің серверіне жіберіледі;</p>
        <p>&nbsp; &nbsp; &nbsp; 2) ВI-RADS жіктеуі бойынша маммограммалардың нәтижелерін (М0, М1, М2, М3, М4, М5) бір медициналық ұйымның (ОО) екі және одан көп тәуелсіз дәрігер-рентгенологтарымен – екіжақты оқу, немесе әртүрлі медициналық ұйымдармен қалалық, аудандық емхананың, (жылжымалы медициналық кешеннің) маммографиялық кабинетінің дәрігер-рентгенологымен – бірінші оқу, және ОО маммография кабинетінің рентгенолог-дәрігерімен екінші оқу жүргізілетін түсіндіру;</p>
        <p>&nbsp; &nbsp; &nbsp; 3) тереңдетілген диагностика - ОО маммография кабинетінде жүргізілетін маммографияларда патологиялық ошақтар (BI-RADS жіктемесі бойынша М4, М5) анықталған жағдайда сүт бездерінің нысаналы маммографиясын, ультрадыбыстық зерттеу (бұдан әрі – УДЗ), пункциялық биопсия немесе гистологиялық зерттеулерге стереотаксиялық пункция жүргізу.</p>
        <p>&nbsp; &nbsp; &nbsp; 45. МСАК ұйымдарының орта медицина қызметкері немесе МСАК ұйымдарының жауапты адамы аудандық, қалалық емханаларға пациентті маммографияға жібереді.</p>
        <p>&nbsp; &nbsp; &nbsp; 46. Қалалық, аудандық емхананың (жылжымалы медициналық кешеннің) маммографиялық кабинетінің рентгензертханашысы:</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;№ 907 бұйрықпен бекітілген маммографиялық скринингке жататын пациенттерді есепке алу журналын, маммограмманы екіжақты оқуға жолдаманы толтырады;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;бірінші оқуды жүргізген кезінде № 907 бұйрықпен бекітілген № 025-08/е нысанына және екінші (қос) оқудың маммограмма жолдамасына бірінші оқудың нәтижелерін енгізеді;</p>
        <p>&nbsp; &nbsp; &nbsp; екіжақты (екінші) оқудың нәтижелерін алған кезінде № 907 бұйрықпен бекітілген № 025-08/е есеп нысанына және маммографиялық скринингке жататын пациенттерді есепке алу журналына екіжақты (екінші) оқудың нәтижелерін енгізеді.</p>
        <p>&nbsp; &nbsp; &nbsp; 47. Қалалық, аудандық емхананың (жылжымалы медициналық кешеннің) маммографиялық кабинетінің рентгенолог-дәрігері:</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;маммографиялық зерттеулердің қауіпсіздігі мен сапасына талаптарды орындайды;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; ұсынылған суреттердің сапасы мен дұрыс салынуын бағалайды;</p>
        <p>&nbsp; &nbsp; &nbsp; BI-RADS жіктеуі бойынша нәтижелерін түсіндірумен маммограммалардың бірінші оқуын жүргізеді;</p>
        <p>&nbsp; &nbsp; &nbsp; маммограммаларды, скринингтік маммограммалардың электрондық көшірмелерін маммограмманы екіжақты оқуға жолдамамен бірге архивтеу және ОО маммография кабинеттің серверіне медициналық суреттерді беру жүйесі арқылы жібереді;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;48. ОО маммография кабинетінің рентгенолог-дәрігері:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;ұсынылған суреттердің сапасы мен дұрыс салынуын бағалайды;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;BI-RADS&nbsp;жіктеуі бойынша нәтижелерін түсіндірумен маммограмманы екіжақты (екінші) оқуды жүргізеді. Көрсетілім бойынша үшінші оқуды ұйымдастырады;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;МСАК ұйымына екіжақты (екінші) оқу нәтижелерін (жолдаманың жыртпалы бөлігін) жібереді;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;МСАК ұйымдарына көрсетілімдері бойынша жүргізетін тереңдетілген диагностикаға (кейіннен гистологиялық зерттеумен жасалатын стереотаксиялық пункция немесе пункциялық биопсия, нысаналы маммография, сүтбезінің УДЗ) пациентті шақыруды ұсынады;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;скрининг шеңберінде жасалған барлық маммограммаларды (пленкалар және электрондық тасымалдаушылар) жинауды және архивтеуді жүзеге асырады. Маммограммаларды сақтау мерзімі – скринингтік жастан шығудан кейінгі 3 жылдан кем емес. Скрининг кезінде жасалған маммограммалар пациентке берілмейді.</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;49. Тереңдетілген диагностикалау үшін көрсетілімдер маммограммаларын екіжақты оқу М4 (қатерлі ісікке күдік тудыратын белгілер), М5 (қатерліліктің іс жүзіндегі сенімді белгілері) қорытындылары болып табылады.&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;50. Маммографияның нәтижесін алу кезінде МСАК ұйымдарының дәрігері немесе МСАК ұйымдарының жауапты адамы:&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;М0 кезінде (қосымша суреттер немесе алдыңғы зерттеудің деректері қажет) - қалалық, аудандық емхананың (жылжымалы медициналық кешеннің) маммография кабинетіне қайта рентгенологиялық зерттеуге пациентті жібереді немесе архив кескіндерімен (олардың бар болған жағдайында) салыстырудың нәтижелері бойынша ОО маммография кабинеті рентгенолог-дәрігерінің ұсыныстарын орындайды;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;М1 кезінде (өзгерістер анықталмады) – пациентке келесі маммографиялық тексеріп-қарауды 2 жылдан кейін жүргізуді ұсынады;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;М2 кезінде (қатерсіз өзгерістер) –пациентті КДБ/КДО онколог (маммолог) консультациясына, 2 жылдан кейін скринингтік маммографиялық зерттеуден өтуге жібереді;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;М3 кезінде (ықтимал қатерсіз өзгерістер) – пациентті 6 айдан кейін бақылау маммографиясын өту ұсынысымен жалпы практика дәрігеріне немесе учаскелік дәрігеріне қысқамерзімді динамикалық байқауға жібереді;&nbsp;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;М4 (қатерлі ісікке күдік тудыратын белгілер) М5 (қатерліліктің іс жүзіндегі анықталған белгілері) кезінде – тереңдетілген диагностикалаудың нәтижелеріне сәйкес пациентті ОО онкологына, маммологына консультацияға және динамикалық байқауға жібереді. Қатерлі ісіктің жоқтығы дәлелденген жағдайда, көрсетілген маммография нәтижелерімен пациент КДБ, КДО онколог (маммолог)-ына емдеу және кейіннен динамикалық байқау мен сауықтыру үшін науқастың тіркелген орны бойынша МСАК мамандарына жібереді;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;амбулаториялық пациенттің медициналық картасына ОО маммография кабинетінің рентгенолог-дәрігерінің қорытындысы мен нәтижелерімен екіжақты (екінші) оқуға маммограмма жолдамасының жыртпалы бөлігі жабыстырады.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;51. Колоректалдық обырде ерте анықтау скринингі сатысы мыналардан тұрады:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) зерттеуді жүргізу ережелерін түсіндіріп және тестті алғаннан кейін үй жағдайларында жүргізілетін иммунохимиялық немесе иммунохроматографиялық әдісіне негізделген гемокульт-тестті жүргізу;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) оң гемокульт-тесті болған жағдайда ішек сілемейінің патологиялық учаскелері анықталу жағдайында биопсиямен жаппай колоноскопияны жүргізу.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;Жаппай колоноскопия эндоскопты өңдеу уақытының баспа функциясы бар эндоскопиялық жабдықтың автоматтық өңдеу аппаратымен бейнетүсірілім мүмкіндіктерімен жіңішке спектральды эндоскопияның қолдауымен бейнеэндоскопиялық жабдықтармен және биопсия алуға арналған және азинвазиялық хирургиялық араласуларды (жіңішке ішекте және т.б.полиптерді алып тастау) жүргізу үшін аспаптармен жабдықталған колоноскопия бөлімшелерінде/кабинеттерінде жүргізіледі.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;52. МСАК орта медициналық қызметкері гемокульт тестті жүргізу үшін таза, құрғақ ыдысқа салынған нәжістің кішкентай бөлігі пайдаланылатынын, материалды алу нәжістің әр жерлеріне контейнер ұшын енгізумен жүргізу керектігін, материалды алғаннан кейін қақпақты жауып және контейнерді бірнеше рет қағу, тест-картаның арнайы терезесіне нәжістің өлшенген бөліктерімен 3 тамшы ерітіндіні жағылатынын айтады, 3-10 минуттан кейін (немесе тестті өндірушінің көрсеткені бойынша) тестті түсіндіруі жүргізіледі.&nbsp;Көрсеткіштер бойынша МСАК жағдайында жүргізіледі.</p>
        <p>&nbsp; &nbsp; &nbsp; 53. Егер тест үй жағдайында жасалса тест нәтижесін бағалауды пациенттің өзі немесе емханада медицина қызметкері жүргізеді:</p>
        <p>&nbsp; &nbsp; &nbsp; Т (тест) және С (бақылау) деңгейінде екі жолақтың пайда болуы оң нәтиже ретінде есептеледі (нәжісте қан анықталу);</p>
        <p>&nbsp; &nbsp; &nbsp; С (бақылау) деңгейінде бір жолақтың пайда болуы теріс нәтиже ретінде есептеледі (нәжісте қан анықталмау);</p>
        <p>&nbsp; &nbsp; &nbsp; Т (тест) деңгейінде бір жолақтың пайда болуы қате нәтиже ретінде есептеледі;</p>
        <p>&nbsp; &nbsp; &nbsp; С (бақылау) деңгейінде жолақтың болмауы қате нәтиже ретінде есептеледі.</p>
        <p>&nbsp; &nbsp; &nbsp; 54. Егер тест үй жағдайында орындалатын болса, пациентті немесе оның туысқанын Т және С деңгейлерімен жолақтарын көрсетуімен тест нәтижелерін қағазға суреттеп немесе нәтиженің дұрыстығын дәлелдеу үшін камераға, телефонға суретке түсіріп алуын сұрау керек.</p>
        <p>&nbsp; &nbsp; &nbsp; 55. Гемокульт-тест нәтижелерін алған кезінде МСАК ұйымдарының орта медицина қызметкері немесе МСАК ұйымдарының жауапты адамы:</p>
        <p>&nbsp; &nbsp; &nbsp; № 907 бұйрықпен бекітілген № 025-08/е нысанын толтырады, скрининг-тест бойынша сауалнама (29,38,43,44 тармақтар) жүргізеді;</p>
        <p>&nbsp; &nbsp; &nbsp; гемокульт-тест нәтижелерін бағалайды (оң, теріс).</p>
        <p>&nbsp; &nbsp; &nbsp; Егер дұрыс нәтиже болмаса тестіні қайталау керек болады.</p>
        <p>&nbsp; &nbsp; &nbsp; Гемокульт-тестісінің оң нәтижесін МСАК ұйымдарының дәрігерімен немесе МСАК ұйымдарының жауапты адамы растауы қажет.</p>
        <p>&nbsp; &nbsp; &nbsp; Тест нәтижелері пациентке және МСАК ұйымдарының орта медицина қызметкеріне немесе МСАК ұйымдарының жауапты адамына оларды алған күннен кейін 3 жұмыс күні ішінде жеткізілуі тиіс.</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;56. Гемокульт-тесттің оң нәтижесінде пациент жуан ішектің бүкіл бөлігіне тексеріп-қарауға (бұдан әрі – жаппай колоноскопия) жіберіледі.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; 57. МСАК ұйымдарының дәрігері немесе МСАК ұйымдарының жауапты адамы:</p>
        <p>&nbsp; &nbsp; &nbsp; гемокульт-тест нәтижелерін колоректалдық скринингке жататын пациенттерді тіркеу журналына енгізеді;</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;пациентке тексеріп-қарауды жүргізу, тексерілу әдістемесі, колоноскопияға дайындық жүргізудің қажеттілігі туралы ақпарат береді;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; оң гемокульт-тест жағдайында жүргізілетін жаппай колоноскопияға жолдайды.</p>
        <p>&nbsp; &nbsp; &nbsp; 58. Эндоскопия бөлімшесінің дәрігері:</p>
        <p>&nbsp; &nbsp; &nbsp; көрсетілімдер бойынша ісіктердің сілемейіне биопсия жүргізеді. Жуан ішектің шырышты биоптаттық гистологиялық зерттеуі патоморфологиялық зертханада немесе патологоанатомиялық бюрода жүргізіледі. Биоптаттық морфологиялық талдауы Дүниежүзілік денсаулық сақтау ұйымының ұсынысына сәйкес жүзеге асырылады;</p>
        <p>&nbsp; &nbsp; &nbsp; жіңішке аяққа тиісті аспаптар болған жағдайда полиптер анықталған кезде клипс салу немесе электрокоагуляция арқылы бір сәттік полипэктомия немесе 0,5 см дейін мөлшеріндегі полиптер кезінде "суық" биопсия жүргізеді;</p>
        <p>&nbsp; &nbsp; &nbsp; колоноскопиялық зерттеу бланкіне нәтижелерді (тікелей колоноскопиядан кейін және гистологиялық қорытындыны алғаннан кейін) енгізеді;</p>
        <p>&nbsp; &nbsp; &nbsp; эндоскопиялық зерттеуден өтетін адамдарға жұқтырмау мақсатында эндоскопиялық ем-шараларды қауіпсіз жүргізу жөніндегі санитариялық-гигиеналық іс-шаралар кешенін жүзеге асырады.</p>
        <p>&nbsp; &nbsp; &nbsp; 59. Бір эндоскопиялық аппаратты пайдаланған жағдайда алдыңғы және келесі эндоскопиялық зерттеулердің арасындағы интервал тазартуды, жоғары деңгейдегі дезинфекция мен эндоскопиялық жабдықты автоматты тазарту аппаратын пайдалану арқылы эндоскопты жууды және үрлеуді ескере отырып, кемінде 20 минутты құрайды.</p>
        <p>&nbsp; &nbsp; &nbsp; 60. Эндоскопия бөлімшесінің мейіргері:</p>
        <p>&nbsp; &nbsp; &nbsp; зерттеу жүргізу күнін, пациенттің тегін, атын, әкесінің атын, жасын, мекенжайын көрсете отырып скрининг уақытында орындалған жуан ішекті эндоскопиялық зерттеудің жүргізілуін тіркеу журналында тіркейді;</p>
        <p>&nbsp; &nbsp; &nbsp; ілеспелі бланкте "Материал скрининг бойынша алынды" деп белгіні көрсете отырып алынған биопсиялық материалды таңбалайды, тіркейді белгіленген қағидаларға сәйкес патоморфологиялық зертханаға немесе патологоанатомиялық бюроға және жібереді;</p>
        <p>&nbsp; &nbsp; &nbsp; МСАК ұйымына эндоскопиялық зерттеу нәтижелерін жібереді;</p>
        <p>&nbsp; &nbsp; &nbsp; 61. МСАК ұйымдарының дәрігері немесе МСАК ұйымдарының жауапты адамы:</p>
        <p>&nbsp; &nbsp; &nbsp; 1) гемокульт-тесттің нәтижелерін алу кезінде:</p>
        <p>&nbsp; &nbsp; &nbsp; теріс (нәжісте жасырын қан болмағанда) 2 жылдан кейін скрининг жүргізуді ұсынады;</p>
        <p>&nbsp; &nbsp; &nbsp; оң (нәжісте жасырын қан болғанда) болса, бірақ пациент қол қойған және амбулаториялық пациенттің медициналық картасына жапсырылған пациенттің немесе оның заңды өкілдерімен қол қойылған колоноскопиядан бас тарту жағдайы немесе эндоскопиялық зерттеу жүргізуге медициналық қарсы айғақтары болған жағдайда пациентпен сұхбат өткізіп, бақылау гемокульт-тестін жүргізе отырып, қауіп-қатер тобына қосады, КДБ, КДО хирург немесе колонопроктологына консультацияға жібереді.</p>
        <p>&nbsp; &nbsp; &nbsp; 62. Эндоскопиялық зерттеулердің нәтижелерін алу кезінде:</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;Патологиясыз (бұдан әрі - CS 1) зерттеліп-қаралған адамдарға 2 жылдан кейін скрининг шеңберінде гемокульт-тест жүргізуді ұсынады;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; жуан ішектің тұқым қуалаушылық ауруы және оның даму аномалиясы, ішектің созылмалы қабыну аурулары (бұдан әрі - CS 2-3 ) пациенттерді КДБ, КДО гасторэнтерологына немесе хирургына немесе колопроктологына жібереді;</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;полип тәрізді ісіктер (бұдан әрі - CS 4) пациенттерге емделуді (полипэктомия), полипэктомиядан кейін 4-6 айдан соң бақылау колоноскопиясын жүргізу арқылы аудандық онкологтың, КДБ, КДО хирургтың немесе колопроктологтың консултациясымен олардың тіркелу орны бойынша амбулаториялық-емханалық ұйымында МСАК мамандарында динамикалық бақылауда болуды ұсынады;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; морфологиялық верификациямен немесе онсыз жуан ішектің қатерлі ісігі (бұдан әрі - CS 5-8) кейін тұрғылықты жері бойынша аудандық онкологта динамикалық байқауда болуымен ОО-қа жібереді.</p>
        <p>&nbsp; &nbsp; &nbsp; 62. Скринингтік қарап-тексерудің қорытынды сатысы өзіне анықталған патологиясы бар адамдарды толық тексеріп-қарау және диспансерлік есепке алу, есепке алу-есеп беру статистикалық құжаттарын ресімдеуді аяқтауды қамтиды. Бұл кезеңде МСАК ұйымының дәрігері немесе МСАК ұйымының жауапты адамы халықтың ересектерді скринингтік қарап-тексерудің нәтижелерін қауіп факторларын (мінез-құлықтық, биологиялық), қорытынды диагнозды № 907 бұйрықпен бекітілген № 025-08/е нысанына, амбулаториялық пациенттің медициналық картасына, сондай-ақ "Емхана" автоматтандырылған ақпараттық жүйесіне енгізеді, толық тексерілуге, байқауға ұсынымдар береді.</p>
        <p>&nbsp; &nbsp; &nbsp; 63. Скринингтік қарап-тексеру аяқталғаннан кейін МСАК ұйымдарының дәрігері немесе МСАК ұйымдарының жауапты адамы бейінді мамандардың қорытындысы және зертханалық-диагностикалық зерттеулерді ескере отырып, ересектерді мынадай диспансерлік байқау топтарына бөледі:</p>
        <p>&nbsp; &nbsp; &nbsp; 1) 1А тобы - қауіп факторынсыз дені саулар: қандайда бір шағым жасамайтын және анамнезінде және тексеру кезінде созылмалы ауруы, қауіп факторы немесе жекелеген ағзалары мен жүйелерінің бұзылулары анықталмаған адамдар;</p>
        <p>&nbsp; &nbsp; &nbsp; 2) 1Б тобы - қауіп факторы бар дені саулар: бақылауды және профилактикалық араласуды қажет ететін "шекаралық" жағдайындағы, қауіп факторлары анықталған (ағзаның функциялық қызметіне әсер етпейтін артериялық қысым өлшемдерінде және басқа да физиологиялық сипаттамаларында белгіленген норма шегінен аздаған ауытқулары анықталған) адамдар;</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;3) 2-топ – іс жүзінде дені саулар: анемнезінде жіті және соңғы 2 жыл бойы қабынусыз келген созылмалы аурулары бар адамдар;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;4) 3-топ - науқастар: динамикалық байқауды, емдеуді және сауықтыруды қажет ететін адамдар.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; 21. Дені сау (1А, 1Б топтары) және іс жүзінде дені сау ересектерге (2 топ):</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;осы Қағидаларға 2-қосымшаға сәйкес кезеңділікпен МСАК ұйымдарында кейінгі скринингтік қарап-тексерулер;</p>
        <p>&nbsp; &nbsp; &nbsp; МСАК ұйымдары мен салауатты өмір салты қызметінің салауатты өмір салты кабинеттеріне, аурулардың бейіні бойынша мектепке сауықтыру өтеді.</p>
        <p>&nbsp; &nbsp; &nbsp; 65. Скринингтік қарап-тексерулер барысында анықталған созылмалы аурулары және (немесе) жекелеген жіті аурулары бар адамдар диспансерлік есепке алынуға және денсаулықтарын нығайту мен қалпына келтіруге бағытталған, кейіннен сауықтыру және де оларға кешенді емдік-профилактикалық іс-шараларын өткізу арқылы динамикалық байқауға жатады.</p>
        <p>&nbsp; &nbsp; &nbsp; 67. Скринингтік қарап-тексерудің әрбір қатысушысы сәйкес ұсынымдар беріле отырып, тексеріп-қараудың нәтижелері туралы ақпараттандырылады.</p>
        <p><strong>&nbsp;3-бөлім. Халықтың нысаналы топтарын скринингтік қарап-тексерулерді өткізудің кезеңділігі</strong></p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;67. Балалардың нысаналы топтарының скринингтік қарап-тексеруі осы Қағидаларға 1-қосымшаға балаларды скринингтік қарап-тексеруді өткізу кезеңділігіне сәйкес жүргізіледі.</p>
        <p>&nbsp; &nbsp; &nbsp; Туғаннан бастап үш жасқа дейінгі жетім балаларға, ата-анасының қамқорлығынсыз қалған балаларға, туғаннан бастап төрт жасқа дейінгі, психикасының және дене бітімінің дамуында кемістігі бар балаларға арналған, баладан бас тарту қаупі бар отбасыларды психологиялық-педагогикалық қолдауды жүзеге асыратын денсаулық сақтау ұйымдарында, балалар үйінді, мектеп-интернаттарда, арнайы интернаттарда, жасөспірімдер үйінде орналасқан балалар скринингтік қарап-тексерулерді жылына екі рет өтеді.</p>
