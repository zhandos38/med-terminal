        <p><strong>&nbsp;Халықтың нысаналы топтарын профилактикалық медициналық қарап-тексеруді өткізу қағидалары мен кезеңділігі&nbsp;</strong></p>
        <p><strong>&nbsp;1-бөлім. Жалпы ережелер</strong></p>
        <p>&nbsp; &nbsp; &nbsp; 1. Осы Халықтың нысаналы топтарын профилактикалық медициналық қарап-тексеруді өткізу қағидалары мен кезеңділігі (бұдан әрі – Қағидалар) халықтың нысаналы топтарын профилактикалық медициналық қарап-тексеруді өткізу тәртібін және кезеңділігін айқындайды.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2. Осы бұйрықта пайдаланылатын терминдер мен анықтамалар:</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;1) Бетесда терминологиялық жүйесі – цитологиялық зерттеу нәтижесін стандарттау;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) динамикалық байқау - халық денсаулығының жай-күйін жүйелі түрде байқау, сондай-ақ осы байқаудың нәтижелері бойынша қажетті медициналық көмек көрсету<strong>;</strong></p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;3) "Емхана" автоматтандырылған ақпараттық жүйесі – "Амбулаториялық пациенттің статистикалық картасын" автоматты түрде енгізуге және өңдеуге, медициналық ұйымдардың есептік құжаттарын сақтауға және алуға, балалар мен ересектердің профилактикалық медициналық (скринингтік) қарап-тексерулерді жүргізуге арналған ақпараттық жүйе;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;4) Жүрек-қантамырының жалпы қаупін бағалау шкаласы – жақын 10 жылда жүрек-қантамыр ауруларының өлім қаупін бағалау шкаласы (бұдан әрі –&nbsp;SCORE&nbsp;шкаласы);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;5) Кетле индексі – ағзаның дене дамуының үйлесімділігін бағалау көрсеткіші, дененің салмағының бойдың ұзындығына қатынасы;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;6) сүт безін сәулелік зерттеудің нәтижесін талдау және хаттамалау жүйесінің жіктеуі – маммографиялық зерттеуді стандарттау (бұдан әрі - ВI-RADS&nbsp;жіктеуі).</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;7) халықтың нысаналы топтарын профилактикалық медициналық қарап-тексерулер – бұл ауруларды ерте сатыларында анықтау және аурулардың өршуінің, аурулардың пайда болуына әсер ететін қатер факторларының алдын алу, халықтың денсаулығын қалыптастыру мен нығайту мақсатында жаппай әдіспен жүргізілетін жаппай профилактикалық медициналық (скринингтік) қарап-тексерулер (бұдан әрі – скринингтік қарап-тексерулер).</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;3. Скринингтік қарап-тексерулер Қазақстан Республикасының 2014 жылғы 16 мамырдағы "Рұқсаттар және хабарламалар туралы" Заңының 28-бабына сәйкес осы қызметтің түріне лицензиясы бар денсаулық сақтау субъектілерімен тегін медициналық көмектің кепілдік берілген көлемінің шеңберінде жүргізіледі.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;4. Облыстардың, республикалық маңызы бар қала мен астананың денсаулық сақтауды мемлекеттік басқарудың жергілікті органдары (бұдан әрі – Басқарма):</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) осы Қағидаларға сәйкес халықтың нысаналы топтарына жыл сайын скринингтік қарап-тексерулерді жүргізуді;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) скринингтік қарап-тексерулердің электрондық деректер базасын толтыруды;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;3) тұрақты жұмыс істейтін скринингтік қарап-тексерулерді үйлестіру, мониторингі және талдау жөніндегі жұмыс тобын құруды қамтамасыз етеді.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;5. Скринингтік қарап-тексерулер динамикалық бақылаумен және сауықтырумен медициналық-санитариялық алғашқы көмек (бұдан әрі – МСАК ұйымдары) көрсететін мынадай денсаулық сақтау ұйымдарымен жүзеге асырылады:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) қала халқы үшін –меншік нысанына қарамастан МСАК ұйымдары;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) ауыл халқы үшін – медициналық пункттер, фельдшерлік-акушериялық пункттер, дәрігерлік амбулаториялар, арнайы автокөлік пен темір жол көлігі базасындағы жылжымалы медициналық кешендерді (пойыз) пайдаланумен аудандық емханалар.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;6. МСАК ұйымдары:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) медициналық ұйымға тіркелген халық санынан скринингтік қарап-тексеруге жататын адамдардың нысаналы тобын қалыптастырады;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) осы қарап-тексеруді жүргізу үшін бейінді медициналық ұйымдармен сабақтастықты қамтамасыз етеді;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;3) халықты скринингтік қарап-тексеруден өту қажеттілігі туралы ақпараттандырады;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;4) амбулаториялық пациенттің медициналық картасына немесе баланың даму тарихына қарап-тексеру нәтижелерін енгізе отырып есептік нысандарды толтырады;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;5) денсаулық сақтау саласында уәкілетті орган, денсаулық сақтау ұйымдарының медициналық ақпараттық жүйелері арқылы (бұдан әрі – АЖ) скринингтік қарап-тексерулердің электрондық базасын толтырады;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;6) скринингтік қарап-тексерулердің ай сайынғы талдауын жүргізеді.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;7. Ауруларының бейіні бойынша диспансерлік есепте тұрған адамдар осы скринингтік қарап-тексерулерге жатпайды.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;8. Қосымша диагностикалық зерттеулер мен бейінді мамандардың қарап-тексерулері көрсетілімдер бойынша жүргізіледі.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;9. МСАК ұйымдарының орта медициналық персоналы немесе МСАК ұйымдарының жауапты адамы скринингтік қарап-тексеру нәтижесі бойынша скрининг қарап-тексеруге шақырылған нысаналы топтың тізімі мен скриннг өткен және толық тексеріп-қарауға жолданғандар тізімін салыстырады.</p>
        <p><strong>&nbsp;2-бөлім. Халықтың нысаналы топтарын скринингтік қарап-тексеруді өткізу тәртібі</strong></p>
        <p><strong>&nbsp;1-тарау. Балаларды скринингтік қарап-тексеруді өткізу тәртібі&nbsp;</strong></p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;10. Мектепке дейінгі мекемелерге баратын жастағы балаларды, мектеп оқушыларын, орта-арнайы және жоғары оқу орындарының 18 жасқа дейінгі оқушылары мен студенттерінің нысаналы топтарын скринингтік қарап-тексерулер жүргізуді аумақтық МСАК ұйымдарының мамандары білім беру ұйымдарының аумағына шыға отырып жүргізеді.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;11. Мектепке дейінгі мекемелерге бармайтын мектепке дейінгі жастағы балаларды скринингтік қарап-тексеру бекіткен жері бойынша МСАК ұйымдарында жүргізіледі.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;12. 18 жасқа дейінгі балаларды скринингтік қарап-тексеруді жүргізу дайындық сатысын, скрининг және қорытынды сатыны қамтиды.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;13. Дайындық сатысы 18 жасқа дейінгі нысаналы топтарды қалыптастыру және ақпараттық сүйемелдеуден тұрады. Дайындық сатысын МСАК ұйымының: медициналық пункттің, фельдшерлік-акушериялық пункттің, дәрігерлік амбулаторияның, аудандық, қалалық емхананың орта медицина қызметкері, білім беру ұйымдарының (болған жағдайда), оның ішінде мектепке дейінгі мекеменің орта медицина қызметкері жүзеге асырады және мыналарды қамтиды:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;күнтізбелік жылдың 15 қарашасына дейін алда тұрған скринингтік қарап-тексеруге жататын нысаналы топтың жыл сайынғы тізімін, ары қарай ай сайын түзетумен қалыптастыру және құру;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;нысаналы топтағы балалардың ата-аналарын және олардың заңды өкілдерін скринингтік қарап-тексеруді өту қажеттілігі және шарттары туралы хабардар ету;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;мектепке дейінгі мекемелерге баратын балаларды, мектеп оқушыларын, орта-арнайы және жоғары оқу орындарының 18 жасқа дейінгі оқушылары мен студенттерін скринингтік қарап-тексерулер жүргізу үшін аумақтық МСАК ұйымдары мамандарының білім беру ұйымдарының (болған жағдайда), сонымен қатар мектепке дейінгі балалар мекемелері аумағына шығуын ұйымдастыру.</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;14. Скрининг сатысы мамандардың қарап-тексеруі және "Денсаулық сақтау ұйымдарының бастапқы медициналық құжаттарының нысандарын бекіту туралы" Қазақстан Республикасы Денсаулық сақтау министрінің 2010 жылғы 23 қарашадағы № 907 бұйрығымен (Нормативтік құқықтық актілерді мемлекеттік тіркеу тізілімінде № 6697 тіркелген) (бұдан әрі – № 907 бұйрық) бекітілген профилактикалық медициналық тексеріп-қараудың (скрининг) статистикалық карталарын толтыруды қарастыруды.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;15. Скрининг сатысы дәрігерге дейінгі, педиатриялық және мамандандырылған сатыларды қамтиды.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;Дәрігерге дейінгі саты медициналық пункттің, фельдшерлік-акушерлік пункттің, дәрігерлік амбулаторияның, аудандық, қалалық емхананың, білім беру ұйымының (болған жағдайда), мектепке дейінгі мекеменің орта медицина қызметкері жүргізеді және өзіне мыналарды қамтиды:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) бойы мен дене салмағын анықтау;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) ерте шақтағы балалардың (3 жасқа дейінгі) басы және кеудесінің көлемін өлшеу;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;3) жеке және психоәлеуметтік дамуды бағалау;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;4) артериялық қысымды өлшеу (7 және одан үлкен жастағы балалардың);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;5) плантография жүргізу және оны бағалау (5 және одан үлкен жастағы балалардың);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;6) көру өткірлігін анықтау;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;7) есту жітілігін анықтау. Тыныш үй-жайда орта медицина қызметкері мен баланың арасы кемінде 5 метр болатындай қашықтықта 3 және одан үлкен жастағы балалардың екі құлағына кезек-кезек сыбырлап сөйлеу арқылы зерттеу;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;8) тексеріп-қарау деректері бойынша бастапқы медициналық құжатты ресімдеу.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;Педиатриялық сатыны педиатр дәрігер немесе жалпы практика дәрігері жүргізеді және дәрігерге дейінгі қарап-тексерулердің деректерін есепке ала отырып, баланың денсаулық жағдайын, психофизикалық, жыныстық дамуын бағалауды, қатал қарау белгілерін анықтауды қамтиды. Осы саты барысында мыналар жүргізіледі:</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;1) тері жабындарын және бастың шашты бөлігін қарап-тексеру;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;2) көзге көрінетін сілемейлі қабықтарды қарап-тексеру: көз, ауыз қуысы, мұрын конъюнктивиттері. Тістің, тістүйіс жағдайына, қатты таңдай жағдайының биіктігіне, таңдай бадамшаларының көлемі мен түрлеріне, дыбыс функциясына (дауыстың дыбысталуын) көңіл аудару;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;3) қалқанша бездерінің аумағын саусақпен қарап-тексеру;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;4) перифериялық түйіндерді саусақпен қарап-тексеру: жақасты, шаптың, қолтықасты;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;5) қанайналым ағзаларын зерттеу (жүректі қарап-тексеру, аускультация), пульстің жиілігін, толуын, ырғағын анықтау. Аускультация кезінде дыбыстың дауыстылығы мен жиілігі анықталады. Жүрек шуылы анықталған жағдайда әртүрлі ыңғайда (тұру, жату) зерттеулер және мөлшерленген дене жүктемесімен функциялық сынамалар жүргізіледі;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;6) тыныс алу ағзаларын зерттеу (өкпе аускультациясы);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;7) асқорыту ағзаларын зерттеу (ішперде қуысы ағзаларын, оң қабырға асты аумағын, бауырды, эпигастралдық аумақты, көкбауырды, оң және сол мықын аумағын, қасаға үстілік аумақты саусақпен қарап-тексеру). Сілемейдің жағдайына, тілдің, иектің, үстіңгі таңдайдың, тістердің өңезденуіне көңіл аударылады. Созылмалы уыттану симптомдары анықталады (тері жабындарының бозғылттануы, орбиталдық көлеңкелер);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;8) Үш-төрт ай аралығындағы балаларға көрсетілімдері бойынша сан-жамбас буындарының дисплазиясын және жамбастың туа болған шығуын ерте анықтау мақсатында сан-жамбас буындарын ультрадыбыстық зерттеу жүргізіледі;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;9) жыныс ағзаларын қарап-тексеру. 10 жастан асқан қыздарды қарап-тексеру кезінде гинекологиялық анамнезге, шағымдарына, етеккір функциясының бұзылуына аса көңіл аударылады;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;10) туғаннан 17 жасты қоса алғанда даму тобы нұсқасын анықтай отырып, нервтік-психикалық дамуы (бұдан әрі – НПД); 1-НПД қалыпты деңгейі; 2 - НПД-ғы сәл ауытқулар; 3 - НПР айтарлықтай ауытқулар.</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;3 жасқа дейінгі балалардың НПД бағалау "Скрининг ұйымдастыру бекіту туралы ұйымдастыру ережесін бекіту туралу" Қазақстан Республикасы Денсаулық сақтау министрінің 2010 жылғы 9 қыркүйектегі № 704 бұйрығына (Нормативтік құқықтық актілерді мемлекеттік тіркеу тізілімінде № 6490 болып тіркелген) сәйкес жүргізіледі.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;4-6 жаста ойлау және сөйлеу, қимылдық даму, назар мен ес, әлеуметтік байланыстар бағаланады.</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;7-8 жаста: психоқимылдық сала мен мінез-құлық, интеллектуалдық даму, эмоциялық-вегетативтік сала.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;9-10 жаста абстрактілік-логикалық операциялардың қалыптасуын, логикалық талдауларын бағалау қосылады.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;11-17 жаста мыналар анықталады: эмоциялық-вегетативтік сала, соматикалық-вегетативтік құбылыстар, вегетивтік-диэнцефалиялық құбылыстар;</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;11) пубертограммалар әдісімен жыныстық дамуын бағалау, қорытынды беріледі (норма, озу, қалып қою);</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;12) осы жасқа тән емес жарақаттар мен оның салдарын анықтау арқылы озбырлық белгілерін, ұру белгілерін (таяқпен немесе шыбықпен ұрғаннан кейінгі сызық қанталаулар, белбеумен, жіппен, ұрғаннан кейін ілгек түріндегі қанталаулар, жіппен немесе белбеумен байлау, тарту ідері, сигаретпен күйдіру іздері, көз торшасына қан құйылу, субдуралдық қан ұюлар). Зорлауға күдік туындаған жағдайда балалардың құқықтарын қорғау органдарына хабарлау қажет және психологтың консультациясына жіберу қажет;</p>
        <p>&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;13) скринингтік қарап-тексеруді аяқтағаннан кейін баланың денсаулық жағдайы туралы қорытынды түрінде ресімделген денсаулықты кешендік бағалау жүргізіледі. Баланың денсаулық жағдайы туралы қорытынды қауіп факторларының болуын анықтауды, физикалық, нервтік-психикалық дамуын бағалауды, резистенттілікті, динамикалық бақылау тобын анықтауды қамтиды.&nbsp;Қорытындыда санитариялық-гигиеналық ережені, режімді, дене тәрбиесін және шынығуды сақтау, психологиялық-педагогикалық іс-шаралар жөніндегі, профилактикалық егулер жүргізу жөніндегі, шекті жағдайы мен аурулардың алдын алу бойынша ұсынымдар беріледі.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; Мамандандырылған саты маман дәрігерлермен жүргізіледі және есепке алу-есептілік құжаттарды толтыра отырып қарап-тексеруді қамтиды:</p>
        <p>&nbsp; &nbsp; &nbsp; 1) хирург-ортопед ерте шақтағы балаларда кіндік, шап сақиналарының кеңеюіне, ішінің ақ сызығының бұлтиған жарығына, крипторхизмге, тік ішектің түсуіне, аяқ-қолының, кеудесінің қисаюына назар аударады. Мектеп жасына дейінгі және мектеп жасындағы балалардың омыртқаларының қисаюын, отырысының, ірі және ұсақ буындары функцияларының бұзылуын анықтайды. Табан күмбезінің жағдайын қарап-тексеруді, плантограмманы бағалауды, жүрісін бағалауды жүргізеді. Ұлдардың ата бездерінің төмен түсуін анықтайды;</p>
        <p>&nbsp; &nbsp; &nbsp; 2) уролог несепжыныс жүйесінің патологиясының жоқ екендігін анықтайды;</p>
        <p>&nbsp; &nbsp; &nbsp; 3) отоларинголог мұрынның алдыңғы риноскопиясын жүргізеді және тыныс алу функциясын тексереді, артқы риноскопиясын, фарингоскопиясын, мойын (жақасты, алдыңғы және артқы мойын, құлақарты) түйіндерін саусақпен қарап-тексеруді, отоскопияны, естуді зерттеуді жүргізеді;</p>
        <p>&nbsp; &nbsp; &nbsp; 4) невролог жалпы қарап-тексеру жүргізеді (дермографиялық сипаттарының, буын суреттерінің және басқалардың болуын анықтау), бассүйек-ми нервтерінің, қимыл-қозғалыс функцияларының жағдайын; сіңірлік, периосттық, тері рефлекстерін зерттеу; вегетативтік реттеуді бағалау;</p>
        <p>&nbsp; &nbsp; &nbsp; 5) стоматолог тістүйіс, пародонт жағдайын, гигиена, РМА (қызылиектің қабыну үрдісін бағалауға папиллярлық маргинальды альвеолярлық индекс), КПУ(бір баладағы кариозды пломбаланған және жұлынған тістердің саны) индекстерін, тісжегінің белсенділік дәрежесін, диспансерлеу, оңалту және профилактика жоспарын бағалай отырып, баланы кешенді қарап-тексеруді жүргізеді;</p>
        <p>&nbsp; &nbsp; &nbsp; 6) офтальмолог көру өткірлігін анықтайды, қарап-тексеру органын сыртқы тексеру жүргізеді; бүйірінен жарық түсіре отырып және өткізу жарығында тексеру, офтальмоскопия жүргізу;</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;7) эндокринолог гинекомастияның, жыныстық дамудың кідіруі мен уақытынан бұрын дамуының, бойының кідіруінің, семіруінің, қалқанша безі ұлғаюының қант диабетінің, крипторхизманың жоқ екендігін анықтау үшін қарап-тексеру жүргізеді.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; 16. Балалар мен жасөспірімдерді скринингтік қарап-тексеру аяқталғаннан кейін бейінді мамандардың қорытындысы мен зертханалық-диагностикалық зерттеулерді ескере отырып, педиатр дәрігер, жалпы практика дәрігері мынадай "денсаулық тобын" анықтай отырып балалардың денсаулық жағдайын кешендік бағалау жүргізеді:</p>
        <p>&nbsp; &nbsp; &nbsp; 1 топ – дені сау балалар;</p>
        <p>&nbsp; &nbsp; &nbsp; 2 топ – дені сау, бірақ функциялық ауытқулары бар, сондай-ақ жіті және созылмалы ауруларға қарсы тұру қабілеті төмен, қауіп факторы бар балалар.</p>
        <p>&nbsp; &nbsp; &nbsp; 3 топ – организмінің функциялық мүмкіндіктері сақталған, компенсация жағдайындағы созылмалы аурумен ауыратын балалар;</p>
        <p>&nbsp; &nbsp; &nbsp; 4 топ - организмінің функциялық мүмкіндіктері төмен, субкомпенсация жағдайындағы созылмалы аурумен ауыратын балалар;</p>
        <p>&nbsp; &nbsp; &nbsp; 5 топ - организмінің функциялық мүмкіндіктері айтарлықтай төмендеген, декомпенсация жағдайындағы созылмалы аурумен ауыратын балалар.</p>
        <p>&nbsp; &nbsp; &nbsp; 17. 2, 3, 4, 5 топтарына жататын балалар МСАК мамандарының немесе тиісті бейін мамандарының динамикалық байқауы мен сауықтыруына жатады.</p>
        <p>&nbsp; &nbsp; &nbsp; 18. Қорытынды саты- анықталған патологиясы бар тұлғаларды толық қарап-тексеру және диспансерлік есепке алу, есепке алу-есеп беру статистикалық құжаттарын ресімдеуді аяқтау.</p>
        <p>&nbsp; &nbsp; &nbsp; 19. Скринингтік қарап-тексеру нәтижесі бойынша денсаулықты кешенді бағалау жүргізіледі, денсаулық тобын көрсетіп физикалық және нервтік-психикалық дамуын бағалап эпикриз бен қорытындыны ресімдейді. Қорытындыда толық қарап-тексеру, бақылау, санитариялық-гигеналық ережелердісақтау, режім, дене тәрбиесі және шынықтыру, профилактикалық екпелерді жүргізу,шекті жағдай мен аурулар профилактикасы, денешынықтыру тобы (негізгі және арнайы топ) бойынша ұсынымдар беріледі.</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;20. Қорытынды сатыда МСАК ұйымның дәрігері немесе МСАК ұйымның жауапты тұлғасы скринингтік тексерудің нәтижелерін № 907 бұйрықпен бекітілген № 025-07/е нысанына, балалар даму тарихына (№ 907 бұйрықпен бекітілген № 112/е-қ нысанына, № 112/е-ұ нысанына), денсаулық куәлігіне "Емхана" автоматтандырылған ақпараттық жүйесіне енгізеді.</p>
        <p>&nbsp; &nbsp; &nbsp; 21. Балаларды скринингтік қарап-тексеру нәтижелері (эпикриз бен қорытындының көшірмелері) ата-аналарға немесе олардың заңды өкілдеріне хабарланады.</p>
        <p><strong>&nbsp;2-тарау. Ересектерді скринингтік қарап-тексеруді өткізу тәртібі</strong></p>
        <p>&nbsp; &nbsp; &nbsp; 22. Ересектерді скринингтік қарап-тексеруді өткізу өзіне дайындық сатысын, скрининг және қорытынды сатыны қамтиды.</p>
        <p>&nbsp; &nbsp; &nbsp; 23. Дайындық сатысы нысаналы топтарды қалыптастыру, ақпараттық сүйемелдеу және скринингке шақыру шараларынан тұрады.</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;Дайындық сатысын МСАК ұйымының: медициналық пункттің, фельдшерлік-акушериялық пункттің, дәрігерлік амбулаторияның, аудандық, қалалық емхананың орта медицина қызметкері жүзеге асырады және мыналарды қамтиды:&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; күнтізбелік жылдың 15 қарашасына дейін алда тұрған скринингтік қарап-тексеруге жататын нысаналы топтың жыл сайынғы тізімін, ары қарай ай сайын түзетумен қалыптастыру және құру;</p>
        <p>&nbsp; &nbsp; &nbsp; ересектердің нысаналы топтарын скринингтік қарап-тексеру өтудің қажеттілігі және шарттары туралы хабардар ету;</p>
        <p>&nbsp; &nbsp; &nbsp; халықтың нысаналы топтарын скринингтік қарап-тексеруге шақыру;</p>
        <p>&nbsp; &nbsp; &nbsp; халықтың нысаналы топтарының скринингтік қарап-тексеруді уақтылы өтуін қамтамасыз ету.</p>
