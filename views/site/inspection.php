        <p>&nbsp; &nbsp; &nbsp; &nbsp;25. Колоректалдық обырын ерте анықтауға скринингтік қарап-тексеруді өту кезінде дайындық сатысында МСАК ұйымдарының орта медицина қызметкері немесе МСАК ұйымдарының жауапты адамы нысаналы топтың пациенттерін скринингтен өтуге қойылатын талаптар туралы ақпараттандырады: етеккір кезеңінде, қанталаған геморройда, несепте немесе дефекация кезіндегі айтарлықтар күшенуден кейін қан болғанда зерттеулер жүргізілмейді.&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; &nbsp;26. Скринингтік қарап-тексерудің скрининг сатысы өзіне: скринингтік қарап-тексеруді, зерттеулерді жүргізу және № 907 бұйрықпен бекітілген № 025-08/е нысаны амбулаториялық пациенттің профилактикалық медициналық тексерудің (скринингтің) статистикалық картасын толтыруды қамтиды.</p>
