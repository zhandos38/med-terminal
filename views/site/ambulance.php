                <a href="http://adilet.zan.kz/kaz/docs/V1300008576#z1">бұйрығының</a>күші жойылды деп танылсын.
            </p>
            <p>&nbsp; &nbsp; &nbsp; 4. Осы бұйрықтың орындалуын бақылау Қазақстан Республикасының Денсаулық сақтау вице-министрі Л.М. Ақтаеваға жүктелсін.</p>
