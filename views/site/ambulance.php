        <p>&nbsp; &nbsp; &nbsp; 51. Врач-координатор отделения санитарной авиации:</p>
        <p>&nbsp; &nbsp; &nbsp; изучает поступившую заявку, медицинскую документацию пациента;</p>
        <p>&nbsp; &nbsp; &nbsp; определяет объем медицинской услуги;</p>
        <p>&nbsp; &nbsp; &nbsp; формирует состав МБСА в соответствии с профилем патологии пациента (ов);</p>
        <p>&nbsp; &nbsp; &nbsp; организовывает транспортировку (доставку) санитарным автотранспортом квалифицированных специалистов (МБСА), пациента(ов), органов (части органов) и тканей для последующей трансплантации, биоматериала.</p>
