            <p>&nbsp; &nbsp; &nbsp; 22. Стационарға келуіне қарай ЖМКС бригадасының немесе МСАК жанындағы ЖМК фельдшері немесе дәрігері қабылдау бөлімшесінің дәрігеріне № 907&nbsp;
                <a href="http://adilet.zan.kz/kaz/docs/V1000006697#z1">бұйрықпен</a> бекітілген № 114/е нысаны бойынша жедел медициналық көмек станциясының ілеспе парағын тапсырады.
