        <p>Правила
            <br>&nbsp;оказания скорой медицинской помощи в Республике Казахстан
        </p>
        <p>&nbsp; &nbsp; &nbsp; Сноска. Правила в редакции приказа Министра здравоохранения РК от 08.01.2018&nbsp;
            <a href="http://adilet.zan.kz/rus/docs/V1800016283#z6">№ 2</a> (вводится в действие по истечении десяти календарных дней после дня его первого официального опубликования).
        </p>
        <p>Глава 1. Общие положения</p>
        <p>&nbsp; &nbsp; &nbsp; 1. Настоящие Правила оказания скорой медицинской помощи в Республике Казахстан (далее – Правила) разработаны в соответствии с подпунктом 96)&nbsp;
            <a href="http://adilet.zan.kz/rus/docs/K090000193_#z198">пункта 1</a> статьи 7 Кодекса Республики Казахстан от 18 сентября 2009 года "О здоровье народа и системе здравоохранения" (далее – Кодекс) и определяют порядок оказания скорой медицинской помощи в Республике Казахстан.
        </p>
        <p>&nbsp; &nbsp; &nbsp; 2. В настоящих правилах используются:</p>
        <p>&nbsp; &nbsp; &nbsp; 1) неотложная медицинская помощь (далее – НМП) – медицинская помощь при внезапных острых заболеваниях, травмах, резком ухудшении состояния здоровья, обострении хронических заболеваний, без явных признаков угрозы жизни пациента;</p>
        <p>&nbsp; &nbsp; &nbsp; 2) скорая медицинская помощь (далее – СМП) – форма предоставления медицинской помощи при возникновении заболеваний и состояний у взрослого и детского населения, требующих экстренной медицинской помощи для предотвращения существенного вреда здоровью и (или) устранения угрозы жизни, а также при необходимости транспортировки органов (части органов) и тканей для последующей трансплантации;</p>
        <p>&nbsp; &nbsp; &nbsp; 3) экстренная медицинская помощь – медицинская помощь, требующая безотлагательного медицинского вмешательства для предотвращения существенного вреда здоровью или устранения угрозы жизни при внезапных острых заболеваниях, травмах, резком ухудшении состояния здоровья, обострении хронических заболеваний.</p>
        <p>Глава 2. Порядок оказания скорой медицинской помощи</p>
        <p>&nbsp; &nbsp; &nbsp; 3. Диспетчер станции скорой медицинской помощи (далее – ССМП) принимает на пульте "103" все вызовы от граждан при нарушении состояния здоровья. При поступлении вызова в диспетчерскую службу ССМП, фиксируются следующие данные:</p>
        <p>&nbsp; &nbsp; &nbsp; фамилия, имя, отчество (при его наличии), возраст и пол пациента;</p>
        <p>&nbsp; &nbsp; &nbsp; данные по состоянию пациента и обстоятельства несчастного случая, травмы или заболевания;</p>
        <p>&nbsp; &nbsp; &nbsp; адрес и телефон, а также ориентировочные данные по проезду к месту нахождения пациента.</p>
        <p>&nbsp; &nbsp; &nbsp; 4. Время обработки вызова с момента его получения диспетчером ССМП до передачи для обслуживания бригаде СМП составляет пять минут, в течение которого проводится сортировка по категории срочности вызова.</p>
        <p>&nbsp; &nbsp; &nbsp; 5. Скорая медицинская помощь оказывается фельдшерскими и специализированными (врачебными) бригадами, состав которых определен согласно&nbsp;
            <a href="http://adilet.zan.kz/rus/docs/V1800016283#z103">приложению 1</a> к настоящим Правилам.
        </p>
        <p>&nbsp; &nbsp; &nbsp; 6. Бригады ССМП подчиняются старшему врачу смены ССМП.</p>
        <p>&nbsp; &nbsp; &nbsp; 7. В сельских населенных пунктах для обслуживания вызовов СМП диспетчером районной подстанции ССМП привлекаются специалисты и санитарный автотранспорт ближайших медицинских организаций.</p>
        <p>&nbsp; &nbsp; &nbsp; 8. Принятые диспетчером ССМП вызовы подразделяются на 4 (четыре) категории срочности согласно&nbsp;
            <a href="http://adilet.zan.kz/rus/docs/V1800016283#z115">приложению 2</a> к настоящим Правилам:
        </p>
        <p>&nbsp; &nbsp; &nbsp; 1) вызов 1 (первой) категории срочности – состояние пациента, представляющее непосредственную угрозу жизни, требующее оказания немедленной медицинской помощи;</p>
        <p>&nbsp; &nbsp; &nbsp; 2) вызов 2 (второй) категории срочности – состояние пациента, представляющее потенциальную угрозу жизни без оказания медицинской помощи;</p>
        <p>&nbsp; &nbsp; &nbsp; 3) вызов 3 (третьей) категории срочности – состояние пациента, представляющую потенциальную угрозу для здоровья без оказания медицинской помощи;</p>
        <p>&nbsp; &nbsp; &nbsp; 4) вызов 4 (четвертой) категории срочности – состояние пациента, вызванное острым заболеванием или обострением хронического заболевания, без внезапных и выраженных нарушений органов и систем, при отсутствии непосредственной и потенциальной угрозы жизни и здоровью пациента.</p>
        <p>&nbsp; &nbsp; &nbsp; 9. При поступлении вызовов 1, 2, 3 категории срочности диспетчер ССМП посредством автоматизированной системы управления передает вызова фельдшерским и специализированным (врачебным) бригадам ССМП.</p>
        <p>&nbsp; &nbsp; &nbsp; 10. При поступлении вызовов 4 категории срочности диспетчер ССМП посредством автоматизированной системы управления передает вызова фельдшерским и специализированным (врачебным) бригадам отделения скорой медицинской помощи при первичной медико – санитарной помощи (далее – отделение СМП при ПМСП).</p>
        <p>&nbsp; &nbsp; &nbsp; 11. Время прибытия фельдшерских и специализированных (врачебных) до места нахождения пациента с момента получения вызова от диспетчера ССМП составляет:</p>
        <p>&nbsp; &nbsp; &nbsp; 1 категория срочности – до десяти минут;</p>
        <p>&nbsp; &nbsp; &nbsp; 2 категория срочности – до пятнадцати минут;</p>
        <p>&nbsp; &nbsp; &nbsp; 3 категория срочности – до тридцати минут;</p>
        <p>&nbsp; &nbsp; &nbsp; 4 категория срочности – до шестидесяти минут.</p>
        <p>&nbsp; &nbsp; &nbsp; 12. Фельдшерские и специализированные (врачебные) бригады отделения СМП при ПМСП оказывают медицинскую помощь прикрепленному населению и лицам, находящимся в зоне обслуживания ПМСП круглосуточно.</p>
        <p>&nbsp; &nbsp; &nbsp; 13. Вызовы 4 категории срочности обслуживаются как на уровне отделения СМП при ПМСП, так и путем передачи данной услуги в аутсорсинг в медицинские организации или (субъекты здравоохранения), имеющим государственную лицензию на оказание скорой или неотложной медицинской помощи, в соответствии со строкой 15 приложения 1&nbsp;
            <a href="http://adilet.zan.kz/rus/docs/Z1400000202#z1">Закона</a> Республики Казахстан от 16 мая 2014 года "О разрешениях и уведомлениях".
        </p>
        <p>&nbsp; &nbsp; &nbsp; 14. При угрозе жизни и здоровью сотрудников бригад ССМП и отделения СМП при ПМСП обслуживание вызова осуществляется в присутствии представителей территориальных органов внутренних дел.</p>
        <p>&nbsp; &nbsp; &nbsp; В случае отсутствия представителей территориальных органов внутренних дел бригады ССМП и отделения СМП при ПМСП оповещают диспетчерскую службу о риске угрозы жизни и здоровью посредством рации и (или) мобильной связи. Дальнейшее обслуживание вызова осуществляется в присутствии представителей территориальных органов внутренних дел.</p>
        <p>&nbsp; &nbsp; &nbsp; 15. По результатам данных осмотра, инструментальной диагностики, динамики состояния пациента на фоне или после проведенных лечебных мероприятий, в соответствии с предварительным диагнозом, отражающим причины данного состояния, фельдшером или врачом бригады ССМП или отделения СМП при ПМСП принимается одно из следующих решений:</p>
        <p>&nbsp; &nbsp; &nbsp; транспортировка пациента в медицинскую организацию, оказывающую стационарную помощь (далее – стационар);</p>
        <p>&nbsp; &nbsp; &nbsp; пациент оставлен на месте вызова;</p>
        <p>&nbsp; &nbsp; &nbsp; пациент оставлен на дому (по месту проживания).</p>
        <p>&nbsp; &nbsp; &nbsp; 16. В случае оставления пациента, не нуждающегося в госпитализации, на месте вызова или на дому, бригадой ССМП или отделения СМП при ПМСП предоставляются медицинские рекомендации для дальнейшего обращения в организацию ПМСП (по месту жительства или прикрепления).</p>
        <p>&nbsp; &nbsp; &nbsp; 17. В случае заболевания пациента и необходимости его посещения на дому участковым врачом фельдшером или врачом бригады ССМП или отделения СМП при ПМСП заполняется&nbsp;
            <a href="http://adilet.zan.kz/rus/docs/V1000006697#z679">сигнальный лист</a> для пациента по форме № 110-1/у, утверждҰнной приказом исполняющего обязанности Министра здравоохранения Республики Казахстан от 23 ноября 2010 года № 907 "Об утверждении форм первичной медицинской документации организаций здравоохранения" (зарегистрирован в Реестре государственной регистрации нормативных правовых актов за № 6697) (далее – Приказ № 907).
        </p>
        <p>&nbsp; &nbsp; &nbsp; 18. В случае принятия решения бригадой ССМП или отделения СМП при ПМСП о транспортировке пациента в стационар, диспетчерская служба ССМП информирует приемное отделение стационара о доставке пациента.</p>
        <p>&nbsp; &nbsp; &nbsp; 19. Транспортировка пациента при угрозе его жизни (острая сердечно-сосудистая и дыхательная недостаточность) осуществляется в приемное отделение ближайшей медицинской организации, оказывающей экстренную стационарную медицинскую помощь</p>
        <p>&nbsp; &nbsp; &nbsp; 20. При транспортировке пациента в стационар допускается его сопровождение (не более одного человека) в санитарном автотранспорте.</p>
        <p>&nbsp; &nbsp; &nbsp; 21. Фельдшер или врач бригады ССМП или отделения СМП при ПМСП отмечает время доставки пациента в&nbsp;
            <a href="http://adilet.zan.kz/rus/docs/V1000006697#z678">карте вызова</a> скорой и неотложной медицинской помощи по форме № 110/у, утверждҰнной Приказом № 907.
        </p>
        <p>&nbsp; &nbsp; &nbsp; 22. По прибытию в стационар, фельдшер или врач бригады ССМП или отделения СМП при ПМСП передает врачу приемного отделения&nbsp;
            <a href="http://adilet.zan.kz/rus/docs/V1000006697#z18132">сопроводительный лист</a> станции скорой медицинской помощи по форме № 114/у, утвержденной Приказом № 907.
        </p>
        <p>&nbsp; &nbsp; &nbsp; 23. После передачи пациента фельдшер или врач бригады ССМП или отделения СМП при ПМСП информирует диспетчера ССМП об окончании вызова.</p>
        <p>&nbsp; &nbsp; &nbsp; 24. Время пребывания бригады ССМП или отделения СМП при ПМСП в приемном отделении стационара не должно превышать 10 минут (время для передачи пациента врачу приемного отделения) с момента ее прибытия в стационар, за исключением случаев необходимости оказания скорой медицинской помощи в чрезвычайных ситуациях.</p>
        <p>&nbsp; &nbsp; &nbsp; 25. Приемное отделение стационара, оказывающее скорую медицинскую помощь организует работу согласно
            <a href="http://adilet.zan.kz/rus/docs/V1800016283#z176">приложению 3</a> к настоящим Правилам.
        </p>
        <p>&nbsp; &nbsp; &nbsp; 26. После передачи бригадами СМП или отделения СМП при ПМСП пациента в приемное отделение стационара медицинская сестра проводит распределение поступающих пациентов (медицинскую сортировку по Триаж системе) на группы, исходя из первоочередности оказания экстренной медицинской помощи.</p>
        <p>&nbsp; &nbsp; &nbsp; 27. Медицинская сортировка по Триаж системе проводится непрерывно и преемственно. Весь процесс оценки состояния одного пациента занимает не более 60 секунд. По завершению оценки, пациенты помечаются цветом одной из категорий сортировки, в виде специальной цветной бирки либо цветной ленты.</p>
        <p>&nbsp; &nbsp; &nbsp; 28. Согласно медицинской сортировке, условно выделяют 3 группы пациентов:</p>
        <p>&nbsp; &nbsp; &nbsp; первая группа (красная зона) – пациенты, состояние которых представляет непосредственную угрозу жизни или имеющие высокий риск ухудшения и требующие экстренной медицинской помощи;</p>
        <p>&nbsp; &nbsp; &nbsp; вторая группа (желтая зона) – пациенты, состояние которых представляет потенциальную угрозу для здоровья или может прогрессировать с развитием ситуации, требующей экстренной медицинской помощи;</p>
        <p>&nbsp; &nbsp; &nbsp; третья группа (зеленая зона) – пациенты, состояние которых не представляет непосредственной угрозы для жизни и здоровья и не требует госпитализации.</p>
        <p>&nbsp; &nbsp;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; 29. По результатам медицинской сортировки по Триаж системе пациент направляется в соответствующую зону приемного отделения для врачебного осмотра.</p>
        <p>&nbsp; &nbsp; &nbsp; 30. Пациенты третьей группы поступают в палату интенсивной терапии, где реаниматолог продолжает реанимационную помощь, проведенную ранее специалистами скорой медицинской помощи. В случае проведения экстренной операции реаниматолог проводит общий наркоз, регионарную анестезию, местную анестезию в условиях операционной приемного покоя с дальнейшей транспортировкой пациента в отделение анестезиологии, реанимации и интенсивной терапии для пробуждения.</p>
        <p>&nbsp; &nbsp; &nbsp; 31. После стабилизации состояния пациент госпитализируется в профильное отделение стационара.</p>
        <p>&nbsp; &nbsp; &nbsp; 32. Пациент с подозрением на инфекционное заболевание помещается в бокс для мониторинга состояния и определения дальнейшей тактики ведения больного. В случае массового поступления пациентов для правильного разделения больных с подозрением на инфекционные заболевания (острые респираторные вирусные инфекции и кишечные заболевания), один из смотровых кабинетов перепрофилируется в инфекционный бокс.</p>
        <p>&nbsp; &nbsp; &nbsp; 33. При госпитализации пациента в стационар медицинская сестра заполняет&nbsp;
            <a href="http://adilet.zan.kz/rus/docs/V1000006697#z579">медицинскую карту</a> стационарного больного по форме № 003/у, утвержденной Приказом № 907 и сопровождает пациента в профильное отделение.
        </p>
        <p>&nbsp; &nbsp; &nbsp; 34. При отсутствии показаний для госпитализации в стационар врач приемного отделения выдает пациенту заключение о его пребывании в приемном отделении с указанием результатов осмотров, консультаций, диагностических исследований и рекомендаций о дальнейшем лечении в условиях ПМСП. Одновременно медицинской сестрой направляется сообщение в регистратуру организации ПМСП о случае заболевания и оформляет&nbsp;
            <a href="http://adilet.zan.kz/rus/docs/V1000006697#z679">сигнальный лист</a> участковому врачу по форме № 110-1/у, утвержденной Приказом № 907.
        </p>
        <p>&nbsp; &nbsp; &nbsp; 35. Для обеспечения беспрепятственного проезда санитарного автотранспорта, в соответствии с
            <a href="http://adilet.zan.kz/rus/docs/P1400001196#z222">постановлением</a> Правительства Республики Казахстан от 13 ноября 2014 года № 1196 "Об утверждении Правил дорожного движения, основных положений по допуску транспортных средств к эксплуатации, перечня оперативных и специальных служб, транспорт которых подлежит оборудованию специальными световыми и звуковыми сигналами и окраске по специальным светографическим схемам", санитарный транспорт с момента выезда бригады СМП до места вызова и во время транспортировки пациента в стационар подает специальный звуковой сигнал (сирена) на максимальном уровне и включает проблесковый маячок синего и (или) красного цвета.
        </p>
        <p>&nbsp; &nbsp; &nbsp; 36. Один санитарный автомобиль вводится из расчета на 10 тысяч населения и менее, с учетом обращаемости населения. Санитарный автотранспорт оснащается радиосвязью и навигационной системой.</p>
        <p>&nbsp; &nbsp; &nbsp; 37. Оказание скорой медицинской помощи бригадами отделения СМП при ПМСП предусматривает использование неспециализированного легкового автомобиля медицинской помощи организации ПМСП, обеспечивающего своевременную доставку бригады к месту вызова пациента.</p>
        <p>&nbsp; &nbsp; &nbsp; 38. В структуру областных и городских ССМП входят станция СМП, подстанции СМП, районные отделения СМП и отделения санитарной авиации.</p>
        <p>&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; 39. В составе ССМП областей, городов республиканского значения и столицы создаются Call–центры (колл-центры), которые обеспечиваются средствами оперативной связи со службами органов внутренних дел и гражданской защиты населения региона, подстанциями СМП, отделениями СМП при ПМСП, приемными отделениями медицинских организаций и информируют население по вопросам оказания медицинской помощи на догоспитальном этапе.</p>
        <p>&nbsp; &nbsp; &nbsp; 40. ССМП областей, городов республиканского значения и столицы оборудуются автоматизированными системами управления по приему и обработке вызовов и системами, позволяющими вести мониторинг за санитарным автотранспортом посредством навигационных систем, а также системой компьютерной записи диалогов с абонентами и автоматическим определителем номера телефона, с которого поступает вызов. Хранение записей диалогов осуществляется не менее 2 лет.</p>
        <p>&nbsp; &nbsp; &nbsp; 41. ССМП областей, городов республиканского значения и столицы обеспечиваются медицинскими изделиями и лекарственными средствами согласно приложению 4 к настоящим Правилам</p>
        <p>&nbsp; &nbsp; &nbsp; 42. В режиме чрезвычайной ситуации ССМП областей, городов республиканского значения и столицы:</p>
        <p>&nbsp; &nbsp; &nbsp; 1) действуют в рамках межведомственного и межсекторального взаимодействия, согласно
            <a href="http://adilet.zan.kz/rus/docs/P100000608_#z1">постановлению</a>Правительства Республики Казахстан от 17 июня 2010 года № 608 "Об утверждении Правил предоставления, видов и объема медицинской помощи при чрезвычайных ситуациях, введении режима чрезвычайного положения",
            <a href="http://adilet.zan.kz/rus/docs/V1500010983#z3">приказу</a>Министра внутренних дел Республики Казахстан от 3 марта 2015 года № 175 "Об утверждении Правил осуществления государственного &nbsp;учета чрезвычайных ситуаций природного и техногенного характера" (зарегистрирован в Реестре государственной регистрации нормативных правовых актов за № 10983) и&nbsp;
            <a href="http://adilet.zan.kz/rus/docs/V1600014387#z1">Совместному приказу</a> Министра внутренних дел Республики Казахстан от 21 сентября 2016 года № 919 и Министра здравоохранения и социального развития Республики Казахстан от 21 сентября 2016 года № 819, Министра по инвестициям и развитию Республики Казахстан от 28 сентября 2016 года № 688 "Об утверждении Правил оперативного реагирования и оказания своевременной комплексной помощи лицам, пострадавшим в дорожно-транспортных происшествиях" (зарегистрирован в Реестре государственной регистрации нормативных правовых актов за № 14387);
        </p>
        <p>&nbsp; &nbsp; &nbsp; 2) направляют в зону чрезвычайной ситуации бригады СМП по ликвидации медико-санитарных последствий чрезвычайных ситуаций;</p>
        <p>&nbsp; &nbsp; &nbsp; 3) проводят лечебно-эвакуационные мероприятия пострадавшим при ликвидации чрезвычайных ситуаций;</p>
        <p>&nbsp; &nbsp; &nbsp; 4) экстренно предоставляют информацию о пострадавших при чрезвычайной ситуации природного и техногенного характера, происшествиях и случаях, обратившихся или доставленных для оказания медицинской помощи, а также умерших от полученных травм в территориальные подразделения уполномоченного органа в сфере гражданской защиты, местный орган государственного управления здравоохранения области, городов республиканского значения, столицы и Национальный координационный центр экстренной медицины.</p>
        <p>&nbsp; &nbsp; &nbsp; 43. При чрезвычайной происшествии, с числом пострадавших 3 и более человек, бригада СМП первая прибывшая на место чрезвычайной ситуации в первую очередь осуществляет медицинскую сортировку.</p>
