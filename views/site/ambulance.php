                <a href="http://adilet.zan.kz/kaz/docs/V1000006697#z1">бұйрықпен</a> бекітілген 003/е нысаны бойынша стационарлық науқастың медициналық картасын толтырады және пациентті бейінді бөлімшеге алып жүреді.
