            <p>Қазақстан Республикасында жедел медициналық көмек көрсету қағидалары</p>
            <p>&nbsp; &nbsp; &nbsp; Ескерту. Қағида жаңа редакцияда – ҚР Денсаулық сақтау министрінің 08.01.2018&nbsp;
                <a href="http://adilet.zan.kz/kaz/docs/V1800016283#z3">№ 2</a> (алғашқы ресми жарияланған күнінен кейін күнтізбелік он күн өткен соң қолданысқа енгізіледі) бұйрығымен.
            </p>
            <p>1-тарау. Жалпы ережелер</p>
            <p>&nbsp; &nbsp; &nbsp; 1. Осы Қазақстан Республикасында жедел медициналық көмек көрсету қағидалары (бұдан әрі – Қағидалар) "Халық денсаулығы және денсаулық сақтау жүйесі туралы" Қазақстан Республикасының 2009 жылғы 18 қыркүйектегі Кодексінің 7-бабы 1-тармағының&nbsp;
                <a href="http://adilet.zan.kz/kaz/docs/K090000193_#z2065">96) тармақшасына</a> сәйкес Қазақстан Республикасында жедел медициналық көмек көрсету тәртібін айқындайды.
            </p>
            <p>&nbsp; &nbsp; &nbsp; 2. Осы Қағидаларда мынадай ұғымдар пайдаланылады:</p>
            <p>&nbsp; &nbsp; &nbsp; 1) кезек күттірмейтін медициналық көмек (бұдан әрі - ККМК) – пациенттің өміріне төнген қауіптің анық белгілері болмағанда, кенеттен болған қатты аурулар, жарақаттар кезіндегі, денсаулық жағдайы күрт нашарлаған, созылмалы аурулары асқынған кездегі медициналық көмек;</p>
            <p>&nbsp; &nbsp; &nbsp; 2) жедел медициналық көмек (бұдан әрі - ЖМК) – денсаулыққа келетін елеулі зиянды болдырмау (немесе) өмірге төнген қатерді жою үшін шұғыл және жедел медициналық көмекті талап ететін аурулар мен жай-күй туындаған кезде, сондай-ақ кейіннен транспланттау үшін ағзаларды (ағзалардың бөліктерін) тасымалдау қажет болған кезде медициналық көмек ұсыну нысаны;</p>
            <p>&nbsp; &nbsp; &nbsp; 3) шұғыл медициналық көмек – кенеттен болған қатты аурулар, жарақаттар, денсаулық жағдайының күрт нашарлауы, созылмалы аурулардың асқынуы кезіндегі денсаулыққа елеулі зиянды болдырмау немесе өмірге төнген қауіпті жою үшін кезек күттірмейтін медициналық араласуды қажет ететін медициналық көмек.</p>
            <p>2-тарау. Жедел медициналық көмек көрсету тәртібі</p>
            <p>&nbsp; &nbsp; &nbsp; 3. Азаматтардың денсаулық жағдайы бұзылған кезде жедел медициналық көмек станциясының (бұдан әрі - ЖМКС) диспетчері "103" пультінде барлық шақыртуларды қабылдайды. Шақырту ЖМКС диспетчерлік қызметіне келіп түскен кезде мынадай деректер белгіленеді:</p>
            <p>&nbsp; &nbsp; &nbsp; пациенттің тегі, аты, әкесінің аты (болған жағдайда), жасы мен жынысы;</p>
            <p>&nbsp; &nbsp; &nbsp; пациенттің жай-күйі бойынша деректер мен жазатайым жағдайдың, жарақаттардың немесе аурудың мән-жайы;</p>
            <p>&nbsp; &nbsp; &nbsp; мекенжайы мен телефоны, сондай-ақ пациенттің орналасқан орнына жету бойынша бағдарлық деректер.</p>
            <p>&nbsp; &nbsp; &nbsp; 4. Шақыртуларға жеделділік санаты бойынша іріктеу жүргізілетін, ЖМК диспетчері шақыртуды алған сәттен бастап қызмет көрсету үшін ЖМК бригадасына бергенге дейін шақыртуды өңдеу уақыты бес минутты құрайды.</p>
            <p>&nbsp; &nbsp; &nbsp; 5. Жедел медициналық көмекті құрамы осы Қағидаларға&nbsp;
                <a href="http://adilet.zan.kz/kaz/docs/V1700015473#z88">1-қосымшаға</a> сәйкес айқындалған фельдшерлік және мамандандырылған (дәрігерлік) бригадалар көрсетеді.
            </p>
            <p>&nbsp; &nbsp; &nbsp; 6. ЖМК бригадасы ЖМКС ауысымының аға дәрігеріне бағынады.</p>
            <p>&nbsp; &nbsp; &nbsp; 7. Ауылдық елді мекендерде ЖМК шақыртуларына қызмет көрсету үшін ЖМКС аудандық кіші станциясының диспетчері жақын жердегі медициналық ұйымдардың мамандары мен санитариялық автокөлікті тартады.</p>
            <p>&nbsp; &nbsp; &nbsp; 8. ЖМКС диспетчері қабылдаған шақыртулар осы Қағидаларға&nbsp;
                <a href="http://adilet.zan.kz/kaz/docs/V1700015473#z90">2-қосымшаға</a> сәйкес 4 (төрт) жеделділік санатына бөлінеді:
            </p>
            <p>&nbsp; &nbsp; &nbsp; 1) 1 (бірінші) жеделділік санатындағы шақырту – өміріне тікелей қауіп төндіретін, жедел медициналық көмек көрсетуді қажет ететін пациенттің жай-күйі;</p>
            <p>&nbsp; &nbsp; &nbsp; 2) 2 (екінші) жеделділік санатындағы шақырту – өміріне медициналық көмек көрсетусіз әлеуетті қауіп төндіретін пациенттің жай-күйі;</p>
            <p>&nbsp; &nbsp; &nbsp; 3) 3 (үшінші) жеделділік санатындағы шақырту – денсаулығына медициналық көмек көрсетусіз әлеуетті қауіп төндіретін пациенттің жай-күйі;</p>
            <p>&nbsp; &nbsp; &nbsp; 4) 4 (төртінші) жеделділік санатындағы шақырту – жіті ауруынан немесе созылмалы ауруының асқынуынан туындаған, ағзалар мен жүйелердің кенеттен болған және айқын көрінетін бұзылуларынсыз пациенттің өмірі мен денсаулығына тікелей және әлеуетті қауіп төндірмейтін пациенттің жай-күйі.</p>
            <p>&nbsp; &nbsp; &nbsp; 9. 1, 2, 3-жеделділік санаттарының шақыртулары келіп түскен кезде ЖМКС диспетчері автоматты басқару жүйесі арқылы шақыртуларды ЖМКС фельдшерлік және мамандандырылған (дәрігерлік) бригадаларына береді.</p>
            <p>&nbsp; &nbsp; &nbsp; 10. 4-жеделділік санатының шақыртулары келіп түскен кезде ЖМКС диспетчері автоматты басқару жүйесі арқылы шақыртуларды медициналық санитариялық алғашқы көмек жанындағы жедел медициналық көмек бөлімшесінің (бұдан әрі – МСАК жанындағы ЖМК бөлімшесі) фельдшерлік және мамандандырылған (дәрігерлік) бригадаларына береді.</p>
            <p>&nbsp; &nbsp; &nbsp; 11. Фельдшерлік және мамандандырылған (дәрігерлік) бригадалардың ЖМКС диспетчерінен шақыртуды қабылдаған сәттен бастап, пациенттің тұрған жеріне дейін жету уақыты:</p>
            <p>&nbsp; &nbsp; &nbsp; 1-жеделділік санаты – он минутқа дейінгі;</p>
            <p>&nbsp; &nbsp; &nbsp; 2-жеделділік санаты – он бес минутқа дейінгі;</p>
            <p>&nbsp; &nbsp; &nbsp; 3-жеделділік санаты – отыз минутқа дейінгі;</p>
            <p>&nbsp; &nbsp; &nbsp; 4-жеделділік санаты – алпыс минутқа дейінгі уақытты құрайды.</p>
            <p>&nbsp; &nbsp; &nbsp; 12. МСАК жанындағы ЖМК бөлімшесінің фельдшерлік және мамандандырылған (дәрігерлік) бригадалары тіркелген халыққа және МСАК қызмет көрсету аймағындағы адамдарға тәулік бойы медициналық қызмет көрсетеді.</p>
            <p>&nbsp; &nbsp; &nbsp; 13. 4 жеделділік санатындағы шақыртулар МСАК жанындағы ЖМК бөлімшесі деңгейіндегі сияқты "Рұқсаттар және хабарламалар туралы" 2014 жылғы 16 мамырдағы Қазақстан Республикасының Заңы&nbsp;
                <a href="http://adilet.zan.kz/kaz/docs/Z1400000202#z246">1-қосымшасының</a> 15-тармағына сәйкес осы көмектің түрін көрсетуге мемлекеттік лицензиясы бар медициналық ұйымдарға (денсаулық сақтау субъектілеріне) немесе жедел және кезек күттірмейтін медициналық көмек көрсетуге лицензиясы бар тұлғаларға аутсорсингке беру арқылы да көрсетіледі.
            </p>
            <p>&nbsp; &nbsp; &nbsp; 14. ЖМКС бригадаларының және МСАК жанындағы ЖМК қызметкерлерінің өміріне және денсаулығына қауіп төнген кезде шақыртуға қызмет көрсету, аумақтық ішкі істер органдары өкілдерінің қатысуымен жүзеге асырылады.</p>
            <p>&nbsp; &nbsp; &nbsp; Аумақтық ішкі істер органдарының өкілдері болмаған жағдайда ЖМК бригадаларының қызметкерлері рация және (немесе) мобильдік байланыс арқылы диспетчерлік қызметті ЖМКС бригадалары және МСАК жанындағы ЖМК бөлімшелері өмірі мен денсаулығына төнген қауіптер туралы хабардар етеді. Шақыртуға одан әрі қызмет көрсету аумақтық ішкі істер органдары өкілдерінің қатысуымен жүзеге асырылады.</p>
            <p>&nbsp; &nbsp; &nbsp; 15. Тексеріп-қарау, аспаптық диагностика, мамандардың консультациясы, осы жағдайлардың себептері көрсетілетін алдын ала диагноздарға сәйкес жүргізілген емдік іс-шаралардың аясында немесе одан кейін пациент жағдайы динамикасының нәтижелері бойынша ЖМКС бригадасының немесе МСАК жанындағы ЖМК бөлімшесінің фельдшері немесе дәрігері мынадай шешімдердің бірін қабылдайды:</p>
            <p>&nbsp; &nbsp; &nbsp; пациентті стационарлық көмек көрсететін (бұдан әрі - стационар) медициналық ұйымға тасымалдау;</p>
            <p>&nbsp; &nbsp; &nbsp; пациентті шақырту орнында қалдыру;</p>
            <p>&nbsp; &nbsp; &nbsp; пациентті үйде қалдыру (тұрғылықты жері бойынша).</p>
            <p>&nbsp; &nbsp; &nbsp; 16. Емдеуге жатқызуды қажет етпейтін пациент шақырту орнында немесе үйде қалдырылған жағдайда ЖМКС бригадасы немесе МСАК жанындағы ЖМК бөлімшесі оған одан әрі жүгіну үшін (тұрғылықты жері немесе бекітілуі бойынша) МСАК ұйымына медициналық ұсыныстар ұсынады.</p>
            <p>&nbsp; &nbsp; &nbsp; 17. Пациент ауырған және учаскелік дәрігердің, фельдшердің немесе ЖМКС не МСАК жанындағы ЖМК бөлімшесі дәрігерінің пациентке белсенді баруы қажет болған, сондай-ақ пациент емдеуге жатқызудан бас тартқан жағдайда ЖМК бригадасының фельдшері немесе дәрігері "Денсаулық сақтау ұйымдарының бастапқы медициналық құжаттама нысандарын бекіту туралы" Қазақстан Республикасы Денсаулық сақтау министрінің міндетін атқарушының 2010 жылғы 23 қарашадағы № 907&nbsp;
