            <p>&nbsp; &nbsp; &nbsp; 23. Пациентті бергеннен кейін ЖМКС бригадасының немесе МСАК жанындағы ЖМК фельдшері немесе дәрігері ЖМКС диспетчерін шақыртудың аяқталғаны туралы хабардар етеді.</p>
            <p>&nbsp; &nbsp; &nbsp; 24. ЖМКС немесе МСАК жанындағы ЖМК бөлімшесі бригадасының стационардың қабылдау бөлімшесінде болу уақыты жаппай жарақаттанулар, уланулар кезінде жедел медициналық көмек көрсету қажеттілігі жағдайларын қоспағанда, оның стационарға келген сәтінен бастап 10 минуттан аспауы (пациентті қабылдау бөлімшесінің дәрігеріне беруге арналған уақыт) тиіс.</p>
            <p>&nbsp; &nbsp; &nbsp; 25. Жедел медициналық көмек көрсететін стационардың қабылдау бөлімшесі жұмысын осы Қағидаларға&nbsp;
                <a href="http://adilet.zan.kz/kaz/docs/V1700015473#z91">3-қосымшаға</a> сәйкес ұйымдастырады.
            </p>
            <p>&nbsp; &nbsp; &nbsp; 26. ЖМК бригадасы немесе МСАК жанындағы ЖМК бөлімшесі пациентті стационардың қабылдау бөлімшесіне бергеннен кейін мейіргер бірінші кезекте шұғыл медициналық көмек көрсетілетіндерді алға тарта отырып, келіп түскен пациенттерді топтарға (Триаж жүйесі бойынша медициналық іріктеу) бөледі.</p>
            <p>&nbsp; &nbsp; &nbsp; 27. Триаж жүйесі бойынша медициналық іріктеу үздіксіз және сабақтастықпен жүргізіледі. Бір пациенттің жағдайын бағалау процесі 60 секундтан аспайды. Бағалау аяқталғаннан кейін пациенттер арнайы түрлі-түсті бирка немесе түрлі-түсті лента түріндегі іріктеу санатының бір түсімен белгіленеді.</p>
            <p>&nbsp; &nbsp; &nbsp; 28. Медициналық іріктеуге сәйкес шартты түрде пациенттер 3 топқа бөлінеді:</p>
            <p>&nbsp; &nbsp; &nbsp; бірінші топ (қызыл аймақ) – өміріне тікелей қауіп төнетін немесе нашарлау қаупі жоғары және шұғыл медициналық көмекті талап ететін пациенттің жай-күйі;</p>
            <p>&nbsp; &nbsp; &nbsp; екінші топ (сары аймақ) – өміріне әлеуетті қауіп төнетін немесе жағдайдың дамуымен өршуі мүмкін, шұғыл медициналық көмекті қажет ететін пациенттің жай-күйі;</p>
            <p>&nbsp; &nbsp; &nbsp; үшінші топ (жасыл аймақ) - өмірі мен денсаулығына тікелей қауіп жоқ және емдеуге жатқызуды талап етпейтін пациенттің жай-күйі.</p>
