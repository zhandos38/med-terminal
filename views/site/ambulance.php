        <p>&nbsp; &nbsp; &nbsp; 44. Экстренная медицинская помощь оказывается специалистами, прошедшими обучение по международным признанным стандартам оказания скорой и неотложной помощи: "Базовая реанимация" - Basic Life Support (BLS), "Расширенная сердечно - легочная реанимация" - Advanced Cardiac Life Support (ACLS), "Расширенная сердечно - легочная реанимация в педиатрии" - Pediatric Advanced Life Support (PALS), "Оказание медицинской помощи на догоспитальном этапе при травмах" - Prehospital Trauma Life Support (PHTLS). В целях отработки практических навыков специалистами областных и городских станций скорой помощи по оказанию экстренной медицинской помощи на базе областных и городских ССМП организуются симуляционные кабинеты.</p>
        <p>&nbsp; &nbsp; &nbsp;&nbsp;</p>
        <p>&nbsp; &nbsp; &nbsp; 45. Для обеспечения доступности услуг скорой медицинской помощи сельскому населению, по решению местных органов государственного управления здравоохранения области в труднодоступных населенных пунктах (отсутствие дорожных покрытий, горная местность), участках, отдаленных от доступа медицинской помощи могут создаваться дополнительные подстанции (отделения) областных ССМП.</p>
