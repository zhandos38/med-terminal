            <p>&nbsp; &nbsp; &nbsp; 35. Жол қауіпсіздігін қамтамасыз ету жағдайлары кезінде санитариялық автокөліктің кедергісіз өтіп кетуін қамтамасыз ету үшін санитариялық автокөлік ЖМК бригадасы шыққан кезден бастап шақырту орнына дейін және пациентті стационарға тасымалдау кезінде "Жол жүрісі қағидаларын, Көлік құралдарын пайдалануға рұқсат беру жөніндегі негізгі ережелерді, көлігі арнайы жарық және дыбыс сигналдарымен жабдықталуға және арнайы түсті-графикалық схемалар бойынша боялуға тиіс жедел және арнайы қызметтер тізбесін бекіту туралы" Қазақстан Республикасы Үкіметінің 2014 жылғы 13 қарашадағы № 1196&nbsp;
